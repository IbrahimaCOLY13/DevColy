class StyleDelegate(QStyledItemDelegate):
    """Délégué personnalisé pour la colonne de style dans le TreeView des thèmes."""
    
    def __init__(self, parent=None):
        super(StyleDelegate, self).__init__(parent)
        
    def createEditor(self, parent, option, index):
        """Crée un éditeur (QComboBox) pour la cellule."""
        if not index.parent().isValid():
            # Ne pas créer d'éditeur pour les nœuds de thème (uniquement pour les couches)
            return None
            
        # Créer un QComboBox comme éditeur
        editor = QComboBox(parent)
        
        # Récupérer les informations sur la couche
        theme_item = index.model().itemFromIndex(index.parent())
        layer_item = index.model().itemFromIndex(index.parent().child(index.row(), 0))
        
        if layer_item and layer_item.data(Qt.UserRole) == "layer":
            # Récupérer l'ID de la couche
            layer_id = layer_item.data(Qt.UserRole + 1)
            layer = QgsProject.instance().mapLayer(layer_id)
            
            if layer:
                # Remplir la liste déroulante avec les styles disponibles pour cette couche
                styles = layer.styleManager().styles()
                for style in styles:
                    editor.addItem(style)
                
                # Sélectionner le style actuel
                current_style = index.model().data(index)
                style_index = editor.findText(current_style)
                if style_index >= 0:
                    editor.setCurrentIndex(style_index)
        
        return editor
    
    def setModelData(self, editor, model, index):
        """Met à jour le modèle avec les données de l'éditeur."""
        if not index.parent().isValid():
            return
            
        # Récupérer le nouveau style sélectionné
        new_style = editor.currentText()
        
        # Mettre à jour le modèle
        model.setData(index, new_style, Qt.DisplayRole)
        
        # Récupérer les informations pour mettre à jour le thème
        theme_item = model.itemFromIndex(index.parent())
        layer_item = model.itemFromIndex(index.parent().child(index.row(), 0))
        
        if theme_item and layer_item and layer_item.data(Qt.UserRole) == "layer":
            theme_name = theme_item.text()
            layer_id = layer_item.data(Qt.UserRole + 1)
            
            # Mettre à jour le style dans le thème
            theme_collection = QgsProject.instance().mapThemeCollection()
            theme_state = theme_collection.mapThemeState(theme_name)
            
            # Vérifier si le layer existe toujours
            layer = QgsProject.instance().mapLayer(layer_id)
            if not layer:
                return
                
            # Trouver et mettre à jour l'enregistrement de la couche
            updated = False
            new_records = []
            
            for record in theme_state.layerRecords():
                if record.layer().id() == layer_id:
                    record.currentStyle = new_style
                    updated = True
                new_records.append(record)
            
            if updated:
                # Créer un nouvel état avec les enregistrements mis à jour
                new_state = QgsMapThemeCollection.MapThemeRecord()
                for record in new_records:
                    new_state.addLayerRecord(record)
                    
                # Mettre à jour le thème
                theme_collection.update(theme_name, new_state)
                
                # Forcer l'enregistrement du projet
                QgsProject.instance().write()

class LayerAddDelegate(QStyledItemDelegate):
    """Délégué personnalisé pour ajouter des couches depuis la TreeView."""
    
    def __init__(self, parent=None):
        super(LayerAddDelegate, self).__init__(parent)
        
    def createEditor(self, parent, option, index):
        """Crée une liste déroulante contenant les couches disponibles."""
        # Vérifier si c'est une ligne d'ajout de couche
        item = index.model().itemFromIndex(index)
        if not item or item.data(Qt.UserRole) != "add_layer":
            return None
            
        # Créer un QComboBox comme éditeur
        editor = QComboBox(parent)
        
        # Obtenir le thème parent
        theme_item = index.model().itemFromIndex(index.parent())
        if not theme_item or theme_item.data(Qt.UserRole) != "theme":
            return None
            
        theme_name = theme_item.text()
        
        # Remplir la liste déroulante avec les couches disponibles
        layers = QgsProject.instance().mapLayers().values()
        for layer in layers:
            # Vérifier si la couche n'est pas déjà dans le thème
            already_in_theme = False
            for row in range(theme_item.rowCount() - 1):  # -1 pour exclure la ligne "Ajouter une couche"
                layer_item = theme_item.child(row, 0)
                if layer_item and layer_item.data(Qt.UserRole + 1) == layer.id():
                    already_in_theme = True
                    break
            
            if not already_in_theme:
                editor.addItem(layer.name(), layer.id())
        
        return editor
    
    def setModelData(self, editor, model, index):
        """Ajoute la couche sélectionnée au thème."""
        if not index.isValid():
            return
            
        # Obtenir l'ID de la couche sélectionnée
        layer_id = editor.currentData()
        if not layer_id:
            return
            
        # Obtenir le thème parent
        theme_item = model.itemFromIndex(index.parent())
        if not theme_item or theme_item.data(Qt.UserRole) != "theme":
            return
            
        theme_name = theme_item.text()
        
        # Ajouter la couche au thème
        layer = QgsProject.instance().mapLayer(layer_id)
        if layer:
            # Récupérer la collection de thèmes
            theme_collection = QgsProject.instance().mapThemeCollection()
            theme_state = theme_collection.mapThemeState(theme_name)
            
            # Créer un nouvel enregistrement pour la couche
            layer_record = QgsMapThemeCollection.MapThemeLayerRecord(layer)
            
            # Utiliser le style par défaut et rendre visible
            default_style = layer.styleManager().currentStyle()
            layer_record.currentStyle = default_style
            layer_record.isVisible = True
            
            # Ajouter l'enregistrement au thème
            theme_state.addLayerRecord(layer_record)
            
            # Mettre à jour le thème
            theme_collection.update(theme_name, theme_state)
            
            # Rafraîchir la vue
            dialog = editor.window()
            if hasattr(dialog, 'load_themes'):
                dialog.load_themes()
                dialog.select_theme_by_name(theme_name)
