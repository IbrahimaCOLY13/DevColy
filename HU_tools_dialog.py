    def zoom_to_error(self):
        """Zoome sur l'erreur sélectionnée dans le tableau."""
        # Obtenir la ligne sélectionnée
        current_row = self.table_erreurs.currentRow()
        if current_row < 0:
            return
            
        # Obtenir les informations de l'erreur
        layer_name = self.table_erreurs.item(current_row, 2).text()
        feature_id = int(self.table_erreurs.item(current_row, 3).text())
        
        # Trouver la couche
        layers = QgsProject.instance().mapLayersByName(layer_name)
        if not layers:
            return
            
        layer = layers[0]
        
        # Trouver l'entité
        feature = next(layer.getFeatures(QgsFeatureRequest(feature_id)), None)
        if not feature:
            return
            
        # Zoomer sur l'entité
        canvas = iface.mapCanvas()
        canvas.setExtent(feature.geometry().boundingBox())
        canvas.refresh()
        
        # Sélectionner l'entité
        layer.removeSelection()
        layer.select(feature_id)
    
