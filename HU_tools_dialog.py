    #3) fonction de zoom sur l'erreur topologique
    def zoom_to_topology_error(self):
        """Zoom sur l'erreur topologique sélectionnée dans le tableau."""
        # Obtenir la ligne sélectionnée
        current_row = self.topo_table_erreurs.currentRow()
        if current_row < 0:
            return
        
        # Obtenir la géométrie stockée dans les données utilisateur
        geom = self.topo_table_erreurs.item(current_row, 0).data(Qt.UserRole)
        if not geom:
            return
            
        # Créer une étendue autour de la géométrie avec une marge de 20m
        extent = geom.boundingBox()
        extent.grow(20)
        
        # Zoomer sur cette étendue
        iface.mapCanvas().setExtent(extent)
        iface.mapCanvas().refresh()
        
        # Afficher la géométrie de l'erreur temporairement
        rubber_band = QgsRubberBand(iface.mapCanvas(), QgsWkbTypes.PointGeometry)
        rubber_band.setColor(QColor(255, 0, 0, 128))  # Rouge semi-transparent
        rubber_band.setWidth(5)
        
        if geom.type() == QgsWkbTypes.PointGeometry:
            rubber_band.addPoint(geom.asPoint())
        elif geom.type() == QgsWkbTypes.LineGeometry:
            for point in geom.asPolyline():
                rubber_band.addPoint(point)
        
        # Faire disparaître le rubber band après 3 secondes
        QTimer.singleShot(3000, rubber_band.reset)

    #4) Exporter les erreurs topologiques vers Excel
    def export_topology_errors(self):
        """Exporte les erreurs topologiques en format Excel."""
        # Demander le chemin du fichier de sortie
        file_path, _ = QFileDialog.getSaveFileName(
            self, "Exporter les erreurs", "", "Fichiers Excel (*.xlsx)"
        )
        
        if not file_path:
            return
        
        if not file_path.lower().endswith('.xlsx'):
            file_path += '.xlsx'
        
        try:
            # Créer un DataFrame pandas avec les erreurs affichées
            data = []
            
            for row in range(self.topo_table_erreurs.rowCount()):
                error_data = {
                    'Type': self.topo_table_erreurs.item(row, 0).text(),
                    'Description': self.topo_table_erreurs.item(row, 1).text(),
                    'Couche': self.topo_table_erreurs.item(row, 2).text(),
                    'ID': self.topo_table_erreurs.item(row, 3).text()
                }
                
                # Récupérer les attributs complets de l'entité
                layer_name = self.topo_table_erreurs.item(row, 2).text()
                feature_id = int(self.topo_table_erreurs.item(row, 3).text())
                
                # Chercher la couche correspondante
                for layer in QgsProject.instance().mapLayersByName(layer_name):
                    feature = next(layer.getFeatures(QgsFeatureRequest().setFilterFid(feature_id)), None)
                    if feature:
                        # Ajouter tous les attributs de l'entité
                        for field in layer.fields():
                            field_name = field.name()
                            error_data[f"Attribut_{field_name}"] = str(feature[field_name])
                        break
                        
                data.append(error_data)
                
            # Créer un DataFrame pandas
            import pandas as pd
            df = pd.DataFrame(data)
            
            # Exporter vers Excel
            df.to_excel(file_path, index=False)
            
            QtWidgets.QMessageBox.information(
                self,
                "Export réussi",
                f"Les erreurs ont été exportées vers {file_path}"
            )
            
        except Exception as e:
            QtWidgets.QMessageBox.warning(
                self,
                "Erreur d'export",
                f"Une erreur est survenue lors de l'export: {str(e)}"
            )