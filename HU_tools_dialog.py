        parent_widget = self.CSV_conso.parentWidget()
        
        # Ajouter les widgets au layout du groupe box
        if isinstance(parent_widget, QtWidgets.QGroupBox) and parent_widget.layout():
            parent_layout = parent_widget.layout()
            parent_layout.addWidget(self.excel_sheet_label)
            parent_layout.addWidget(self.excel_sheet_combo)
        
