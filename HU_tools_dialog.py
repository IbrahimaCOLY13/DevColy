    def save_tree_expansion_state(self):
        """Sauvegarde l'état d'expansion de tous les thèmes dans l'arborescence."""
        expansion_state = {}
        
        # Parcourir tous les thèmes (éléments de premier niveau)
        for row in range(self.theme_model.rowCount()):
            item = self.theme_model.item(row, 0)
            theme_name = item.text()
            index = self.theme_model.indexFromItem(item)
            # Sauvegarder si le thème est développé
            expansion_state[theme_name] = self.Theme_tree.isExpanded(index)
            
        return expansion_state

    def restore_tree_expansion_state(self, expansion_state):
        """Restaure l'état d'expansion des thèmes."""
        if not expansion_state:
            return
            
        # Parcourir tous les thèmes actuels
        for row in range(self.theme_model.rowCount()):
            item = self.theme_model.item(row, 0)
            theme_name = item.text()
            index = self.theme_model.indexFromItem(item)
            
            # Si le thème était développé auparavant, le redévelopper
            if theme_name in expansion_state and expansion_state[theme_name]:
                self.Theme_tree.expand(index)

