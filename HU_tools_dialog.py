    def convertir_champ_en_date(self):
        """Convertit un champ texte en champ Date en utilisant l’algorithme QGIS 'native:fieldcalculator' (to_date())."""

        layer_id = self.Listecouche.currentData()
        layer = QgsProject.instance().mapLayer(layer_id)

        if not layer:
            QMessageBox.warning(self, "Erreur", "Aucune couche sélectionnée.")
            return

        selected_rows = self.Champview.selectionModel().selectedRows()
        if not selected_rows:
            QMessageBox.warning(self, "Attention", "Veuillez sélectionner au moins un champ à convertir.")
            return

        longueur = self.Longueur.value()
        if longueur <= 0:
            QMessageBox.warning(self, "Attention", "Veuillez spécifier une longueur valide (supérieure à 0).")
            return

        success_count = 0
        error_fields = []

        # Assure-toi que la couche est en édition
        if not layer.isEditable():
            layer.startEditing()

        source_rows = [self.proxy_model.mapToSource(row) for row in selected_rows]

        for row in source_rows:
            field_name = self.champs_model.item(row.row(), 0).text()
            field_index = layer.fields().indexOf(field_name)

            if field_index == -1:
                error_fields.append(field_name)
                continue

            new_field_name = f"{field_name}_temp"

            # Expression identique à la calculatrice QGIS
            expression = f"to_date(\"{field_name}\", 'dd/MM/yyyy')"

            params = {
                'INPUT': QgsProcessingFeatureSourceDefinition(layer.source(), True),
                'FIELD_NAME': new_field_name,
                'FIELD_TYPE': 14,  # 14 = Date
                'FIELD_LENGTH': longueur,
                'FIELD_PRECISION': 0,
                'FORMULA': expression,
                'OUTPUT': 'TEMPORARY_OUTPUT'
            }

            try:
                # Exécute la calculatrice de champ sur une copie temporaire
                result = processing.run("native:fieldcalculator", params)
                output_layer = result['OUTPUT']

                # Récupère les valeurs converties
                temp_values = {f.id(): f[new_field_name] for f in output_layer.getFeatures()}

                # Applique les valeurs sur la couche en édition
                for fid, val in temp_values.items():
                    if val is not None:
                        layer.changeAttributeValue(fid, field_index, val)
                        success_count += 1

                # Sauvegarde les changements après chaque champ traité
                layer.commitChanges()
                layer.startEditing()  # relancer l'édition pour le prochain champ

            except Exception as e:
                QgsMessageLog.logMessage(f"Erreur sur le champ {field_name}: {e}", "ConversionChamps", Qgis.Warning)
                error_fields.append(field_name)
                layer.rollBack()
                layer.startEditing()  # relancer proprement l'édition

        # Fin de conversion
        if layer.isEditable():
            layer.commitChanges()

        msg = f"{success_count} valeur(s) convertie(s) avec succès."
        if error_fields:
            msg += f"\nErreurs sur : {', '.join(error_fields)}"

        QMessageBox.information(self, "Conversion terminée", msg)


