    def get_common_fields(self, layers):
        """Retourne les champs communs à toutes les couches."""
        if not layers:
            return []
            
        # Obtenir les champs de la première couche
        common_fields = set(field.name() for field in layers[0].fields())
        
        # Intersecter avec les forps des autres couches
        for layer in layers[1:]:
            layer_fields = set(field.name() for field in layer.fields())
            common_fields = common_fields.intersection(layer_fields)
            
        return sorted(list(common_fields))

