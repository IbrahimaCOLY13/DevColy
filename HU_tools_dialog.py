    def apply_selected_qml_style(self):
        """Applique le style QML sélectionné à la couche choisie en créant un nouveau style."""
        # Obtenir le fichier QML sélectionné
        qml_file = self.get_selected_qml_file()
        if not qml_file:
            QtWidgets.QMessageBox.warning(self, "Attention", "Veuillez sélectionner un fichier de style QML.")
            return
        
        # Obtenir la couche sélectionnée
        layer_id = self.Style_couche.currentData()
        if not layer_id:
            QtWidgets.QMessageBox.warning(self, "Attention", "Veuillez sélectionner une couche.")
            return
        
        layer = QgsProject.instance().mapLayer(layer_id)
        if not layer or not isinstance(layer, QgsVectorLayer):
            return
        
        # Déterminer le nom du nouveau style (basé sur le nom du fichier QML)
        style_name = os.path.splitext(os.path.basename(qml_file))[0]
        
        # Vérifier si le style existe déjà
        if style_name in layer.styleManager().styles():
            # Demander confirmation pour écraser le style existant
            result = QtWidgets.QMessageBox.question(
                self,
                "Confirmation",
                f"Le style '{style_name}' existe déjà. Voulez-vous le remplacer?",
                QtWidgets.QMessageBox.Yes | QtWidgets.QMessageBox.No
            )
            
            if result != QtWidgets.QMessageBox.Yes:
                return
        
        # Sauvegarder le style actuel
        current_style = layer.styleManager().currentStyle()
        
        # Appliquer le style à partir du fichier QML
        message, success = layer.loadNamedStyle(qml_file)
        
        if success:
            # Sauvegarder le style actuel sous le nouveau nom
            layer.styleManager().addStyleFromLayer(style_name)
            
            # Restaurer le style précédent
            layer.styleManager().setCurrentStyle(current_style)
            
            QtWidgets.QMessageBox.information(self, "Succès", f"Le style '{style_name}' a été ajouté à la couche '{layer.name()}'.")
            
            # Mettre à jour la liste des styles existants
            self.update_maj_style_list()
            
            # Sélectionner le nouveau style dans la combobox
            index = self.Maj_style_couche.findText(style_name)
            if index >= 0:
                self.Maj_style_couche.setCurrentIndex(index)
        else:
            QtWidgets.QMessageBox.warning(self, "Erreur", f"Impossible d'appliquer le style: {message}")
            
