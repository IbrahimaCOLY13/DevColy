    #pour remplace toutes les valeurs de 'result_name' par celles de la première colonne (champ initial)
    def remplacer_result_name(self):
        """
        Remplace les valeurs de 'result_name' par celles de la première colonne (champ initial)
        pour les lignes sélectionnées dans le tableau,
        met à jour le CSV 'geocoded_mauvais_score.csv' et la couche QGIS 'Adresses géocodées'.
        """
        try:
            # Vérifier qu'une sélection existe
            selection = self.result_geocode.selectionModel()
            if not selection or not selection.hasSelection():
                QMessageBox.warning(self, "Attention", "Veuillez sélectionner au moins une ligne à modifier.")
                return

            proxy_model = self.result_geocode.model()
            if not proxy_model:
                QMessageBox.warning(self, "Erreur", "Aucune table chargée.")
                return

            model = proxy_model.sourceModel() if hasattr(proxy_model, "sourceModel") else proxy_model

            # Chemin du CSV des mauvais résultats
            import pandas as pd
            temp_dir = os.path.join(os.path.dirname(__file__), 'temp')
            mauvais_score_csv = os.path.join(temp_dir, 'geocoded_mauvais_score.csv')

            if not os.path.exists(mauvais_score_csv):
                QMessageBox.critical(self, "Erreur", f"Fichier introuvable :\n{mauvais_score_csv}")
                return

            df = pd.read_csv(mauvais_score_csv, encoding='utf-8-sig')

            if 'result_name' not in df.columns:
                QMessageBox.critical(self, "Erreur", "Le champ 'result_name' est introuvable dans le CSV.")
                return

            # Charger la couche QGIS
            geocoded_layer = None
            for layer in QgsProject.instance().mapLayers().values():
                if layer.name() == "Adresses géocodées" and isinstance(layer, QgsVectorLayer):
                    geocoded_layer = layer
                    break

            if not geocoded_layer or not geocoded_layer.isValid():
                QMessageBox.warning(self, "Erreur", "Couche 'Adresses géocodées' non trouvée ou invalide.")
                return

            # Commencer l'édition si nécessaire
            editing_started_here = False
            if not geocoded_layer.isEditable():
                geocoded_layer.startEditing()
                editing_started_here = True

            idx_result_name = geocoded_layer.fields().indexOf('result_name')

            updated_count = 0

            for index in selection.selectedRows():
                row = index.row()
                new_value = str(model.data(model.index(row, 0)))  # colonne 0 = champ initial
                old_value = str(model.data(model.index(row, 1)))  # colonne 1 = ancien result_name

                if not new_value or not old_value:
                    continue

                #  Mise à jour du CSV 
                mask = df['result_name'].astype(str).str.strip().str.lower() == old_value.strip().lower()
                if mask.any():
                    df.loc[mask, 'result_name'] = new_value
                    updated_count += mask.sum()
                    print(f"CSV : Remplacé '{old_value}' → '{new_value}' ({mask.sum()} ligne(s))")

                #  Mise à jour dans la couche QGIS 
                for feat in geocoded_layer.getFeatures():
                    val = str(feat['result_name']).strip().lower()
                    if val == old_value.strip().lower():
                        geocoded_layer.changeAttributeValue(feat.id(), idx_result_name, new_value)
                        print(f"QGIS : Remplacé '{old_value}' → '{new_value}' (fid={feat.id()})")

                #  Mise à jour du modèle Qt pour refléter le changement dans le tableau 
                model.setData(model.index(row, 1), new_value)

            # Commit QGIS si on a démarré l'édition
            if editing_started_here:
                geocoded_layer.commitChanges()
            geocoded_layer.triggerRepaint()

            # Réécrire le CSV
            df.to_csv(mauvais_score_csv, index=False, encoding='utf-8-sig')
            print(f"CSV mis à jour : {mauvais_score_csv}")

            # Message final
            QMessageBox.information(
                self,
                "Remplacement terminé",
                f"{updated_count} valeurs remplacées dans le CSV (geocoded_mauvais_score) et dans la couche QGIS"
            )

        except Exception as e:
            import traceback
            print("Erreur détaillée :", traceback.format_exc())
            QMessageBox.critical(self, "Erreur", f"Une erreur est survenue : {str(e)}")



