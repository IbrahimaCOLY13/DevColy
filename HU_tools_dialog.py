    # Méthodes de statistiques réseau
    def calculate_network_stats(self, layer: QgsVectorLayer, field: str, use_length: bool = True) -> Dict[str, float]:
        """Calcule les statistiques pour une seule couche réseau et un seul champ."""
        field_stats = {}
        for feat in layer.getFeatures():
            value = feat[field]
            if value is None:
                continue
            # Convertir la valeur QVariant en chaîne de caractères
            value_str = str(value)
            if use_length:
                length = feat.geometry().length()
                field_stats[value_str] = field_stats.get(value_str, 0) + length
            else:
                field_stats[value_str] = field_stats.get(value_str, 0) + 1
        return field_stats

    def calculate_point_stats(self, layer: QgsVectorLayer, field: str) -> Dict[str, float]:
        """Calcule les statistiques pour une seule couche de points et un seul champ."""
        field_stats = {}
        for feat in layer.getFeatures():
            value = feat[field]
            if value is None:
                continue
            # Convertir la valeur QVariant en chaîne de caractères
            value_str = str(value)
            field_stats[value_str] = field_stats.get(value_str, 0) + 1
        return field_stats

