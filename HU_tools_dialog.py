    def activer_edition_resultats(self):
        """Active l'édition des résultats dans la table et connecte les signaux pour mettre à jour la couche."""
        # Configurer la QTableView pour permettre l'édition
        self.result_geocode.setEditTriggers(QtWidgets.QAbstractItemView.DoubleClicked | 
                                          QtWidgets.QAbstractItemView.EditKeyPressed)
        
        # Obtenir le modèle actuel
        model = self.result_geocode.model()
        if not model:
            QMessageBox.warning(self, "Attention", "Aucune donnée à éditer.")
            return
        
        # Connecter le signal de modification des données
        try:
            # Déconnecter d'abord pour éviter les connexions multiples
            model.itemChanged.disconnect()
        except:
            pass  # Si le signal n'était pas connecté, on ignore l'erreur
        
        # Connecter le signal à la fonction de mise à jour
        model.itemChanged.connect(self.appliquer_modifications_geocodage)
        
        # Chercher la couche d'adresses géocodées
        geocoded_layer = None
        for layer in QgsProject.instance().mapLayers().values():
            if layer.name() == "Adresses géocodées" and isinstance(layer, QgsVectorLayer):
                geocoded_layer = layer
                break
        
        # Stocker la couche comme attribut de classe pour y accéder dans d'autres méthodes
        self.geocoded_layer = geocoded_layer
        
        # Informer l'utilisateur
        if geocoded_layer:
            QMessageBox.information(self, "Édition activée", 
                                  "Double-cliquez sur une cellule pour la modifier. "
                                  "Les modifications seront appliquées à la couche 'Adresses géocodées'.")
        else:
            QMessageBox.warning(self, "Attention", "Couche 'Adresses géocodées' non trouvée.")
    
    def appliquer_modifications_geocodage(self, item):
        """Applique les modifications faites dans la table à la couche vectorielle."""
        if not hasattr(self, 'geocoded_layer') or not self.geocoded_layer:
            return
        
        # Obtenir la ligne modifiée
        row = item.row()
        col = item.column()
        
        # Obtenir le modèle et les valeurs de latitude/longitude pour identifier l'entité
        model = self.result_geocode.model()
        try:
            lat = float(model.data(model.index(row, 4)))  # Indice 4 pour latitude
            lon = float(model.data(model.index(row, 5)))  # Indice 5 pour longitude
        except (ValueError, TypeError, IndexError):
            QMessageBox.warning(self, "Erreur", "Impossible d'identifier l'entité à modifier.")
            return
        
        # Trouver l'entité correspondante dans la couche
        field_mappings = [
            ('result_name', 0), ('result_postcode', 1), ('result_city', 2),
            ('result_score', 3), ('result_latitude', 4), ('result_longitude', 5),
            ('result_housenumber', 6), ('result_street', 7), ('result_city', 8),
            ('result_citycode', 9), ('result_type', 10)
        ]
        
        # Trouver le nom du champ correspondant à la colonne modifiée
        field_name = None
        for name, index in field_mappings:
            if index == col:
                field_name = name
                break
        
        if not field_name:
            return  # Colonne non mappée
        
        # Obtenir la nouvelle valeur
        new_value = item.text()
        
        # Commencer l'édition de la couche
        was_editing = self.geocoded_layer.isEditable()
        if not was_editing:
            self.geocoded_layer.startEditing()
        
        # Trouver et mettre à jour l'entité
        feature_updated = False
        for feature in self.geocoded_layer.getFeatures():
            # Comparer les coordonnées avec une petite tolérance
            if (abs(float(feature['result_latitude']) - lat) < 0.001 and 
                abs(float(feature['result_longitude']) - lon) < 0.001):
                # Mettre à jour la valeur de l'attribut
                field_index = self.geocoded_layer.fields().indexOf(field_name)
                if field_index >= 0:
                    self.geocoded_layer.changeAttributeValue(feature.id(), field_index, new_value)
                    feature_updated = True
                    break
        
        # Appliquer les modifications si nous avons commencé l'édition
        if not was_editing:
            self.geocoded_layer.commitChanges()
        
        if feature_updated:
            print(f"Attribut {field_name} mis à jour avec la valeur '{new_value}'")
        else:
            print(f"Aucune entité trouvée correspondant aux coordonnées Lat: {lat}, Lon: {lon}")
    
    # Pour Re-géocode les entités sélectionnées dans la table avec les valeurs modifiées : ont lesscores sont <0.5
    def regeocode_selected_features(self):
        """Re-géocode les entités sélectionnées dans la table avec les valeurs modifiées (score < 0.5)."""
        selection = self.result_geocode.selectionModel()
        if not selection or not selection.hasSelection():
            QMessageBox.warning(self, "Attention", "Veuillez sélectionner au moins une ligne à re-géocoder.")
            return

        # Récupération de la couche "Adresses géocodées"
        geocoded_layer = None
        for layer in QgsProject.instance().mapLayers().values():
            if layer.name() == "Adresses géocodées" and isinstance(layer, QgsVectorLayer):
                geocoded_layer = layer
                break

        if not geocoded_layer or not geocoded_layer.isValid():
            QMessageBox.warning(self, "Erreur", "Couche 'Adresses géocodées' non trouvée ou invalide.")
            return

        was_editing = geocoded_layer.isEditable()
        if not was_editing:
            geocoded_layer.startEditing()

        model = self.result_geocode.model()
        updated_count = 0
        failed_count = 0

        for index in selection.selectedRows():
            row = index.row()
            # On prend l'adresse corrigée si disponible, sinon le champ initial
            adresse = model.data(model.index(row, 1)) or model.data(model.index(row, 0)) or ""
            cp = model.data(model.index(row, 2)) or ""
            ville = model.data(model.index(row, 3)) or ""

            if not adresse.strip():
                print(f"Ligne {row}: champ adresse vide.")
                failed_count += 1
                continue

            params = {'q': f"{adresse} {cp} {ville}", 'limit': 1}
            try:
                response = requests.get(ADDOK_URL, params=params, timeout=10)
                result = response.json()

                if not result.get('features'):
                    print(f"Aucun résultat pour {adresse}")
                    failed_count += 1
                    continue

                feature_data = result['features'][0]
                props = feature_data['properties']
                lon, lat = feature_data['geometry']['coordinates']
                score = props.get('score', 0)
                label = props.get('label', adresse)

                # Trouver la feature correspondante via les coordonnées existantes
                fid = None
                lat_model = float(model.data(model.index(row, 5)) or 0)
                lon_model = float(model.data(model.index(row, 6)) or 0)
                for feat in geocoded_layer.getFeatures():
                    if abs(float(feat['result_latitude']) - lat_model) < 0.001 and \
                       abs(float(feat['result_longitude']) - lon_model) < 0.001:
                        fid = feat.id()
                        break

                if fid is None:
                    print(f"Aucune feature trouvée pour {adresse}")
                    failed_count += 1
                    continue

                # Mise à jour des attributs
                attr_map = {
                    'result_name': label,
                    'result_postcode': props.get('postcode', cp),
                    'result_city': props.get('city', ville),
                    'result_score': score,
                    'result_latitude': lat,
                    'result_longitude': lon,
                    'result_street': props.get('street', ''),
                    'result_citycode': props.get('citycode', ''),
                    'result_type': props.get('type', '')
                }

                for champ, valeur in attr_map.items():
                    idx = geocoded_layer.fields().indexOf(champ)
                    if idx >= 0:
                        geocoded_layer.changeAttributeValue(fid, idx, valeur)

                # Mise à jour de la géométrie
                src = QgsCoordinateReferenceSystem("EPSG:4326")
                dest = QgsCoordinateReferenceSystem("EPSG:2154")
                transform = QgsCoordinateTransform(src, dest, QgsProject.instance())
                point = QgsPointXY(lon, lat)
                geom = QgsGeometry.fromPointXY(transform.transform(point))
                geocoded_layer.changeGeometry(fid, geom)

                updated_count += 1

                # Mise à jour de la table
                model.setItem(row, 1, QStandardItem(label))
                model.setItem(row, 2, QStandardItem(str(cp)))
                model.setItem(row, 3, QStandardItem(str(ville)))
                model.setItem(row, 4, QStandardItem(f"{score:.4f}"))
                model.setItem(row, 5, QStandardItem(f"{lat:.6f}"))
                model.setItem(row, 6, QStandardItem(f"{lon:.6f}"))

            except Exception as e:
                print(f"Erreur API pour {adresse}: {e}")
                failed_count += 1

        if not was_editing:
            geocoded_layer.commitChanges()

        geocoded_layer.triggerRepaint()
        QMessageBox.information(
            self, "Re-géocodage terminé",
            f"{updated_count} entités mises à jour.\n{failed_count} entités n'ont pas pu être mises à jour."
        )

    # Re-géocode automatiquement toutes les adresses à faible score (<0.5), sans séletionner les lignes
    def regeocode_mauvais_resultats(self):
        """
        Re-géocode automatiquement les adresses à faible score (<0.5)
        à partir des colonnes result_name, result_postcode et result_city
        dans le CSV 'geocoded_mauvais_score.csv'.
        Si score >= 0.5, elles sont ajoutées dans geocoded.csv et la couche QGIS,
        sinon elles restent dans geocoded_mauvais_score.csv.
        """
        try:
            import pandas as pd
            from qgis.core import QgsFeature, QgsPointXY, QgsGeometry, QgsProject, QgsVectorLayer
            from PyQt5.QtWidgets import QProgressDialog, QApplication, QMessageBox
            from PyQt5.QtCore import Qt
            import requests
            import os

            temp_dir = os.path.join(os.path.dirname(__file__), 'temp')
            mauvais_score_csv = os.path.join(temp_dir, 'geocoded_mauvais_score.csv')
            geocoded_csv = os.path.join(temp_dir, 'geocoded.csv')

            if not os.path.exists(mauvais_score_csv):
                QMessageBox.warning(self, "Erreur", "Le fichier des mauvais scores n’a pas été trouvé.")
                return

            df_mauvais = pd.read_csv(mauvais_score_csv, encoding='utf-8-sig')
            if df_mauvais.empty:
                QMessageBox.information(self, "Aucun mauvais résultat", "Le fichier des mauvais scores est vide.")
                return

            # Charger la couche QGIS
            geocoded_layer = None
            for layer in QgsProject.instance().mapLayers().values():
                if layer.name() == "Adresses géocodées" and isinstance(layer, QgsVectorLayer):
                    geocoded_layer = layer
                    break

            if not geocoded_layer or not geocoded_layer.isValid():
                QMessageBox.warning(self, "Erreur", "Couche 'Adresses géocodées' non trouvée ou invalide.")
                return

            was_editing = geocoded_layer.isEditable()
            if not was_editing:
                geocoded_layer.startEditing()

            ADDOK_URL = "https://data.geopf.fr/geocodage/search"
            updated_rows = []
            remaining_rows = []

            progress = QProgressDialog("Re-géocodage en cours...", "Annuler", 0, len(df_mauvais), self)
            progress.setWindowModality(Qt.WindowModal)
            progress.show()

            for i, row in df_mauvais.iterrows():
                QApplication.processEvents()
                if progress.wasCanceled():
                    QMessageBox.information(self, "Annulé", "Le re-géocodage a été interrompu.")
                    break

                # Recomposer l’adresse
                parts = []
                for champ in ['result_name', 'result_postcode', 'result_city']:
                    if champ in row and pd.notna(row[champ]) and str(row[champ]).strip():
                        parts.append(str(row[champ]).strip())

                adresse_complete = " ".join(parts)
                if not adresse_complete:
                    remaining_rows.append(row)
                    continue

                try:
                    response = requests.get(ADDOK_URL, params={'q': adresse_complete, 'limit': 1}, timeout=10)
                    data = response.json()

                    if not data.get('features'):
                        remaining_rows.append(row)
                        continue

                    feature = data['features'][0]
                    props = feature['properties']
                    geom = feature['geometry']
                    score = float(props.get('score', 0))

                    # Mise à jour de la ligne
                    row['result_score'] = score
                    row['result_name'] = props.get('label', '')
                    row['result_postcode'] = props.get('postcode', '')
                    row['result_city'] = props.get('city', '')
                    row['result_street'] = props.get('street', '')
                    row['result_citycode'] = props.get('citycode', '')
                    row['result_type'] = props.get('type', '')
                    row['result_longitude'] = geom['coordinates'][0]
                    row['result_latitude'] = geom['coordinates'][1]

                    # Vérification stricte du score
                    if score >= 0.5:
                        updated_rows.append(row)

                        # Ajouter dans QGIS uniquement si score >= 0.5
                        fet = QgsFeature(geocoded_layer.fields())
                        for col in row.index:
                            if col in [f.name() for f in geocoded_layer.fields()]:
                                fet[col] = row[col]
                        point = QgsPointXY(row['result_longitude'], row['result_latitude'])
                        fet.setGeometry(QgsGeometry.fromPointXY(point))
                        geocoded_layer.addFeature(fet)
                    else:
                        remaining_rows.append(row)

                except Exception as e:
                    print(f"Erreur pour {adresse_complete}: {e}")
                    remaining_rows.append(row)

                progress.setValue(i + 1)

            # Nettoyage et commit QGIS
            if not was_editing:
                geocoded_layer.commitChanges()
            geocoded_layer.triggerRepaint()
            progress.close()

            # Nettoyage du geocoded.csv : suppression de toutes les lignes à score < 0.5
            if os.path.exists(geocoded_csv):
                df_geocoded = pd.read_csv(geocoded_csv, encoding='utf-8-sig')
                df_geocoded = df_geocoded[df_geocoded['result_score'].fillna(0) >= 0.5]
            else:
                df_geocoded = pd.DataFrame()

            # Ajout des nouvelles bonnes lignes
            if updated_rows:
                df_updated = pd.DataFrame(updated_rows)
                df_geocoded = pd.concat([df_geocoded, df_updated], ignore_index=True)

            df_geocoded.to_csv(geocoded_csv, index=False, encoding='utf-8-sig')

            # Réécriture du CSV des mauvais résultats
            df_new_mauvais = pd.DataFrame(remaining_rows) if remaining_rows else pd.DataFrame(columns=df_mauvais.columns)
            df_new_mauvais.to_csv(mauvais_score_csv, index=False, encoding='utf-8-sig')

            QMessageBox.information(
                self,
                "Re-géocodage terminé",
                f"{len(updated_rows)} adresses corrigées (score ≥ 0.5) ajoutées.\n"
                f"{len(remaining_rows)} restent à faible score (< 0.5)."
            )

        except Exception as e:
            import traceback
            print(traceback.format_exc())
            QMessageBox.critical(self, "Erreur critique", str(e))

 
 
