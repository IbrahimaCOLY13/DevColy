    def toggle_file_type(self):
        """Gère l'affichage des widgets selon le type de fichier sélectionné"""
        is_excel = self.Excel_conso.isChecked()
        
        # Afficher/masquer les widgets de feuille Excel
        self.excel_sheet_label.setVisible(is_excel)
        self.excel_sheet_combo.setVisible(is_excel)
        self.excel_sheet_combo.setEnabled(is_excel)
        
        # Si le fichier est déjà sélectionné, adapter le traitement au type choisi
        current_file = self.csv_geocode.filePath()
        if current_file and os.path.exists(current_file):
            file_ext = os.path.splitext(current_file)[1].lower()
            is_file_excel = file_ext in ['.xlsx', '.xls', '.xlsm']
            
            # Si le type de fichier sélectionné ne correspond pas à l'extension
            if is_excel != is_file_excel:
                # Réinitialiser le champ de fichier
                self.csv_geocode.setFilePath("")
                
                # Message d'information
                if is_excel:
                    QtWidgets.QMessageBox.information(
                        self,
                        "Type de fichier", 
                        "Veuillez sélectionner un fichier Excel (.xlsx, .xls, .xlsm)."
                    )
                else:
                    QtWidgets.QMessageBox.information(
                        self,
                        "Type de fichier",
                        "Veuillez sélectionner un fichier CSV."
                    )
            else:
                # Si le type correspond, traiter le fichier
                self.on_file_selected(current_file)

    def on_file_selected(self, file_path):
        """Gère la sélection d'un nouveau fichier (CSV ou Excel)"""
        if not file_path or not os.path.exists(file_path):
            return
            
        file_ext = os.path.splitext(file_path)[1].lower()
        is_file_excel = file_ext in ['.xlsx', '.xls', '.xlsm']
        
        # Vérifier si le type de fichier correspond au mode sélectionné
        if is_file_excel != self.Excel_conso.isChecked():
            # Ajuster les boutons radio selon le type de fichier
            self.Excel_conso.setChecked(is_file_excel)
            self.CSV_conso.setChecked(not is_file_excel)
        
        # Charger les données selon le type
        if is_file_excel:
            self.load_excel_sheets(file_path)
        else:
            self.charger_colonnes_csv()

    def load_excel_sheets(self, excel_file):
        """Charge les noms des feuilles du classeur Excel et met à jour le combobox"""
        try:
            # Vérifier si pandas est disponible
            try:
                import pandas as pd
            except ImportError:
                QtWidgets.QMessageBox.warning(
                    self, 
                    "Dépendance manquante", 
                    "Le module 'pandas' est nécessaire pour lire les fichiers Excel.\n"
                    "Veuillez l'installer via pip: pip install pandas openpyxl"
                )
                return
            
            # Bloquer les signaux pendant la mise à jour
            self.excel_sheet_combo.blockSignals(True)
            
            # Vider le combobox
            self.excel_sheet_combo.clear()
            
            # Lire les noms des feuilles
            xls = pd.ExcelFile(excel_file)
            sheet_names = xls.sheet_names
            
            # Ajouter les feuilles au combobox
            self.excel_sheet_combo.addItems(sheet_names)
            
            # Débloquer les signaux
            self.excel_sheet_combo.blockSignals(False)
            
            # Charger les colonnes de la première feuille
            if sheet_names:
                self.excel_sheet_combo.setCurrentIndex(0)
                self.charger_colonnes_excel()
                
        except Exception as e:
            QMessageBox.warning(self, "Erreur", f"Impossible de charger les feuilles Excel: {str(e)}")
            
        # Connecter le signal après le chargement initial
        self.excel_sheet_combo.currentIndexChanged.connect(self.charger_colonnes_excel)

