    def pre_trier_consommation(self):
        """Ajoute les champs Conso_ret et type_conso et les remplit selon les critères."""
        # Vérifier qu'un champ de consommation est sélectionné
        if self.Champs_conso_retenu.currentIndex() < 0:
            QMessageBox.warning(self, "Attention", "Veuillez sélectionner un champ de consommation.")
            return
            
        # Obtenir la couche et le champ sélectionné
        layer_id = self.Couche_geocode_conso.currentData()
        layer = QgsProject.instance().mapLayer(layer_id)
        champ_conso = self.Champs_conso_retenu.currentText()
        
        if not layer:
            return
            
        # Commencer l'édition de la couche
        if not layer.isEditable():
            layer.startEditing()
            
        # Vérifier si les champs existent, sinon créer
        idx_conso_ret = layer.fields().indexOf('Conso_ret')
        if idx_conso_ret == -1:
            new_field = QgsField('Conso_ret', QVariant.Double)
            new_field.setLength(10)
            new_field.setPrecision(3)
            if not layer.addAttribute(new_field):
                QMessageBox.warning(self, "Erreur", "Impossible d'ajouter le champ Conso_ret.")
                return
            idx_conso_ret = layer.fields().indexOf('Conso_ret')
            
        idx_type_conso = layer.fields().indexOf('type_conso')
        if idx_type_conso == -1:
            new_field = QgsField('type_conso', QVariant.String)
            new_field.setLength(50)
            if not layer.addAttribute(new_field):
                QMessageBox.warning(self, "Erreur", "Impossible d'ajouter le champ type_conso.")
                return
            idx_type_conso = layer.fields().indexOf('type_conso')
            
        # Parcourir les entités et mettre à jour les champs avec barre de progression
        idx_conso = layer.fields().indexOf(champ_conso)
        features = list(layer.getFeatures())
        features_count = len(features)
        
        domestique_count = 0
        entreprise_count = 0
        faible_count = 0
        error_count = 0
        
        # Créer la barre de progression
        progress = QProgressDialog("Classification des consommations...", "Annuler", 0, features_count, self)
        progress.setWindowModality(Qt.WindowModal)
        progress.show()
        
        for i, feature in enumerate(features):
            if progress.wasCanceled():
                break  # Permet d'arrêter si l'utilisateur clique sur Annuler
            try:
                conso_value = float(feature[idx_conso]) if feature[idx_conso] else 0
                
                # Copier la valeur dans Conso_ret
                layer.changeAttributeValue(feature.id(), idx_conso_ret, conso_value)
                
                # Déterminer le type de consommation
                if conso_value < Constants.CONSUMPTION_THRESHOLDS['FAIBLE']['max']:
                    type_value = Constants.CONSUMPTION_THRESHOLDS['FAIBLE']['label']
                    faible_count += 1
                elif conso_value <= Constants.CONSUMPTION_THRESHOLDS['DOMESTIQUE']['max']:
                    type_value = Constants.CONSUMPTION_THRESHOLDS['DOMESTIQUE']['label']
                    domestique_count += 1
                else:
                    type_value = Constants.CONSUMPTION_THRESHOLDS['ENTREPRISE']['label']
                    entreprise_count += 1
                    
                layer.changeAttributeValue(feature.id(), idx_type_conso, type_value)
                
            except (ValueError, TypeError):
                error_count += 1
                layer.changeAttributeValue(feature.id(), idx_type_conso, None)
                layer.changeAttributeValue(feature.id(), idx_conso_ret, None)
            
            # Mettre à jour la progression
            progress.setValue(i + 1)
        
        # Sauvegarder les modifications
        if layer.commitChanges():
            msg = f"Classification terminée :\n"
            msg += f"- {domestique_count} consommations domestiques (5-500)\n"
            msg += f"- {entreprise_count} consommations entreprise (>500)\n"
            msg += f"- {faible_count} faibles consommations (<5)"
            if error_count > 0:
                msg += f"\n\n{error_count} valeurs n'ont pas pu être classifiées."
            QMessageBox.information(self, "Résultat", msg)
            layer.triggerRepaint()
        else:
            QMessageBox.warning(self, "Erreur", "Impossible de sauvegarder les modifications.")
            layer.rollBack()
            
        
    def afficher_consommateurs_filtres(self, seuil, comparaison_sup=True):
        """Affiche les consommateurs filtrés selon un seuil et un critère de raccordement."""
        # Obtenir la couche et les champs sélectionnés
        layer_id = self.Couche_geocode_conso.currentData()
        layer = QgsProject.instance().mapLayer(layer_id)
        champ_raccordement = self.Champ_raccordement.currentText()
        critere_raccordement = self.Critere_raccordement.currentText()
        
        if not layer:
            return
            
        # Vérifier si le champ Conso_ret existe
        idx_conso_ret = layer.fields().indexOf('Conso_ret')
        if idx_conso_ret == -1:
            QMessageBox.warning(self, "Attention", "Veuillez d'abord effectuer un pré-tri des consommations.")
            return
            
        # Vider le modèle existant
        self.conso_model.clear()
        
        # Configurer les en-têtes du modèle avec tous les champs de la couche
        headers = [field.name() for field in layer.fields()]
        self.conso_model.setHorizontalHeaderLabels(headers)
        
        # Obtenir les indices des champs
        idx_raccordement = layer.fields().indexOf(champ_raccordement)
        count = 0
        
        for feature in layer.getFeatures():
            try:
                conso_value = float(feature[idx_conso_ret]) if feature[idx_conso_ret] else 0
                raccordement_value = str(feature[idx_raccordement]) if feature[idx_raccordement] else ""
                
                # Vérifier si la valeur correspond aux critères (seuil et raccordement)
                condition_seuil = (comparaison_sup and conso_value > seuil) or (not comparaison_sup and conso_value < seuil)
                condition_raccordement = True if not critere_raccordement else raccordement_value == critere_raccordement
                
                if condition_seuil and condition_raccordement:
                    # Créer une ligne pour cette entité
                    row_items = []
                    for field in layer.fields():
                        value = feature[field.name()]
                        item = QStandardItem(str(value) if value is not None else '')
                        item.setData(feature.id(), Qt.UserRole)  # Stocker l'ID de l'entité
                        row_items.append(item)
                    
                    self.conso_model.appendRow(row_items)
                    count += 1
                    
            except (ValueError, TypeError):
                continue
                
        # Ajuster la largeur des colonnes
        self.apercu_conso.resizeColumnsToContents()
        
        # Afficher un message avec le nombre d'entités trouvées
        message = f"{count} consommateur{'s' if count > 1 else ''} "
        message += f"avec une consommation {'supérieure' if comparaison_sup else 'inférieure'} à {seuil}"
        if critere_raccordement:
            message += f" et {champ_raccordement}={critere_raccordement}"
        QMessageBox.information(self, "Résultat", message)

        # Connecter le signal de modification des données
        self.conso_model.itemChanged.connect(lambda item: self.mettre_a_jour_valeur_couche(item, layer))
      
