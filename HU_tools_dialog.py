    def display_current_graph(self):
        """Affiche le graphique courant dans la vue appropriée selon le mode."""
        if not self.graphs or self.current_graph >= len(self.graphs):
            return
        
        # N'utiliser que webEngineView puisque graphicsView a été supprimé
        self.webEngineView.show()
        self.webEngineView.raise_()
        
        # Récupérer les données du graphique courant
        graph_data = self.graphs[self.current_graph]['data']
        title = self.graphs[self.current_graph]['title']
        
        # Trier les données par valeur décroissante
        items = sorted(graph_data.items(), key=lambda x: x[1], reverse=True)
        labels, values = zip(*items) if items else ([], [])
        
        # Calculer les pourcentages
        total = sum(values)
        percentages = [v/total*100 for v in values]
        
        # Créer la figure selon le type de graphique
        if self.radio_barre.isChecked():
            # Graphique en barres
            fig = go.Figure()
            fig.add_trace(go.Bar(
                x=list(labels),
                y=list(values),
                text=[f"{p:.1f}%" for p in percentages] if self.check_etiquette_pourcentage.isChecked() else None,
                textposition='outside',
                hovertemplate="%{x}<br>%{y} (%{text})<extra></extra>",
                marker_color=px.colors.qualitative.Plotly,
            ))
            
            # Configuration du layout
            fig.update_layout(
                title=title if self.check_titre.isChecked() else None,
                showlegend=self.check_legende.isChecked(),
                xaxis_title=None,
                yaxis_title=None,
                margin=dict(l=30, r=10, t=50, b=50),
                font=dict(family="Arial", size=12, color="black"),
                autosize=True
            )
            
            # Rotation des étiquettes pour meilleure lisibilité
            fig.update_xaxes(tickangle=45)
            
        else:
            # Graphique en camembert ou anneau
            # Préparer les textes pour les annotations
            hover_texts = []
            for i, (label, value) in enumerate(zip(labels, values)):
                text = f"{label}: {value}"
                if self.check_pourcentage.isChecked():
                    text += f" ({percentages[i]:.1f}%)"
                hover_texts.append(text)
            
            # Créer le graphique
            fig = go.Figure()
            fig.add_trace(go.Pie(
                labels=list(labels),
                values=list(values),
                text=hover_texts,
                textinfo='percent' if self.check_etiquette_pourcentage.isChecked() else 'none',
                hoverinfo='text',
                textfont=dict(size=14, color="black"),
                marker=dict(colors=px.colors.qualitative.Plotly),
                hole=0.5 if self.radio_anneau.isChecked() else 0,
                showlegend=self.check_legende.isChecked(),
            ))
            
            # Configuration du layout
            fig.update_layout(
                title=title if self.check_titre.isChecked() else None,
                margin=dict(l=10, r=10, t=50, b=10),
                font=dict(family="Arial", size=12, color="black"),
                autosize=True,
                annotations=[
                    dict(
                        text=f"Total:<br>{total:.1f}",
                        showarrow=False,
                        font=dict(size=16, color="black"),
                        x=0.5,
                        y=0.5,
                        visible=self.radio_anneau.isChecked()
                    )
                ] if self.radio_anneau.isChecked() else [],
            )
            
            # Position de la légende (si visible)
            if self.check_legende.isChecked():
                fig.update_layout(
                    legend=dict(
                        orientation="v",
                        yanchor="middle",
                        y=0.5,
                        xanchor="left",
                        x=1.05,
                        font=dict(color="black")
                    )
                )
            
            # Ajouter les annotations autour du graphique si demandé
            if self.check_annot_connect.isChecked():
                # Uniquement pour les graphiques en camembert ou anneau
                if not self.radio_barre.isChecked():
                    # Calculer les annotations avec connecteurs
                    annotations = []
                    
                    # Paramètres pour les annotations
                    radius = 1.05  # Distance depuis le centre (réduite pour garder dans le cadre)
                    font_size = 10
                    
                    # Calculer la position et l'angle pour chaque annotation
                    for i, (label, value) in enumerate(zip(labels, values)):
                        # Calculer l'angle en radians (position dans le camembert)
                        # Pour l'emplacement des labels, on utilise le milieu de chaque secteur
                        percent = percentages[i]
                        cumulative_percent = sum(percentages[:i]) + percent / 2
                        theta = (cumulative_percent / 100) * 2 * np.pi
                        
                        # Calculer la position x, y (coordonnées cartésiennes)
                        x = radius * np.cos(theta)
                        y = radius * np.sin(theta)
                        
                        # Préparer le texte de l'annotation
                        annotation_text = f"{label}: {value:.1f}"
                        if self.check_pourcentage.isChecked():
                            annotation_text += f" ({percent:.1f}%)"
                        
                        # Déterminer l'alignement du texte selon la position
                        align = "left" if x < 0 else "right"
                        
                        # Créer l'annotation
                        annotation = dict(
                            x=x,
                            y=y,
                            text=annotation_text,
                            showarrow=True,
                            arrowhead=2,
                            arrowsize=1,
                            arrowwidth=1.5,
                            arrowcolor='#636363',
                            ax=-30 if x < 0 else 30,  # Décalage réduit
                            ay=0,
                            font=dict(size=font_size, color="black"),
                            align=align,
                            xanchor="right" if x < 0 else "left",
                            yanchor="middle"
                        )
                        
                        annotations.append(annotation)
                    
                    # Ajouter les annotations à la figure
                    fig.update_layout(annotations=annotations)
                else:
                    # Pour les graphiques en barres, ajouter des annotations au-dessus de chaque barre
                    annotations = []
                    
                    for i, (label, value) in enumerate(zip(labels, values)):
                        annotation_text = f"{value:.1f}"
                        if self.check_pourcentage.isChecked():
                            annotation_text += f" ({percentages[i]:.1f}%)"
                            
                        annotation = dict(
                            x=label,
                            y=value,
                            text=annotation_text,
                            showarrow=True,
                            arrowhead=2,
                            arrowsize=1,
                            arrowwidth=1.5,
                            arrowcolor='#636363',
                            ax=0,
                            ay=-25,  # Décalage réduit
                            font=dict(size=10, color="black"),
                            align="center",
                            xanchor="center",
                            yanchor="bottom"
                        )
                        
                        annotations.append(annotation)
                        
                    # Ajouter les annotations à la figure
                    fig.update_layout(annotations=annotations) 
        
        # Options communes aux deux types de graphiques
        fig.update_layout(
            plot_bgcolor='rgba(0,0,0,0)',
            paper_bgcolor='rgba(0,0,0,0)',
            font=dict(family="Arial", size=12, color="black"),
        )
        
        # Désactiver les animations
        fig.update_layout(
            transition_duration=0,
        )
        
        # Configuration pour s'adapter à la taille de la fenêtre
        fig.update_layout(
            autosize=True,
            width=None,
            height=None
        )

        # Convertir la figure en HTML pour l'afficher dans QWebEngineView
        html = pio.to_html(
            fig, 
            include_plotlyjs='cdn',
            config={'displayModeBar': False, 'responsive': True}
        )
        
        # Ajouter du CSS supplémentaire pour assurer que le graphique remplit la zone disponible
        html = html.replace('<head>', '<head><style>html, body, #graph {width: 100%; height: 100%; margin: 0; padding: 0;}</style>')
        
        # Afficher dans le QWebEngineView
        self.webEngineView.setHtml(html)
        
        # Stocker la figure pour l'export
        self.figure = fig

