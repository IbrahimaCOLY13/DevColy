        geocoded_layer = None
        for layer in QgsProject.instance().mapLayers().values():
            if layer.name() == "Adresses géocodées" and isinstance(layer, QgsVectorLayer):
                geocoded_layer = layer
                break
        if not geocoded_layer:
            QMessageBox.warning(self, "Attention", "Couche d'adresses géocodées non trouvée.")
            return

        feature_ids = []
        extent = None
        model = self.result_geocode.model()

        for index in selection_model.selectedRows():
            row = index.row()
            try:
                lat = float(model.data(model.index(row, 5)))
                lon = float(model.data(model.index(row, 6)))
                for feature in geocoded_layer.getFeatures():
                    if (abs(float(feature['result_latitude']) - lat) < 0.0001 and 
                        abs(float(feature['result_longitude']) - lon) < 0.0001):
                        feature_ids.append(feature.id())
                        if extent is None:
                            extent = QgsRectangle(feature.geometry().boundingBox())
                        else:
                            extent.combineExtentWith(feature.geometry().boundingBox())
                        break
            except (ValueError, TypeError) as e:
                print(f"Erreur lors du traitement des coordonnées: {e}")
                continue

        if not feature_ids or extent is None:
            QMessageBox.warning(self, "Attention", "Aucune entité correspondante trouvée dans la couche.")
            return

        geocoded_layer.removeSelection()
        geocoded_layer.select(feature_ids)

        # Ajouter marge
        extent.grow(0.2)

        canvas = iface.mapCanvas()
        canvas.setExtent(extent)
        canvas.refresh()

        QMessageBox.information(self, "Zoom", f"Zoom sur {len(feature_ids)} entité(s) sélectionnée(s).")


