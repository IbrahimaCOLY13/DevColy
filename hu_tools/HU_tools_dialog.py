    from PyQt5.QtCore import QVariant
    import os, glob, pandas as pd
    def charger_geocoded_csv(self):
        dossier = os.path.join(os.path.expanduser("~"), "Downloads")
        fichiers = glob.glob(os.path.join(dossier, "*geocoded*"))

        if not fichiers:
            self.afficher_message("Aucun fichier geocoded trouvé dans Téléchargements")
            return

        dernier_csv = max(fichiers, key=os.path.getmtime)

        # Lire le fichier
        try:
            df = pd.read_csv(dernier_csv, sep=",", encoding="utf-8", engine="python")
        except:
            df = pd.read_csv(dernier_csv, sep=";", encoding="latin1", engine="python")

        # Colonnes à afficher dans le QTableView
        colonnes = [
            "Adresse de livraison",
            "result_name",
            "longitude",
            "latitude",
            "result_score",
            "result_label",
            "result_type",
            "result_housenumber",
            "result_street",
            "result_postcode",
            "result_city",
            "result_context",
            "result_citycode"
        ]

        # Normaliser les colonnes pour correspondance
        colonnes_trouvees = []
        colonnes_absentes = []
        cols_norm = {c.strip().lower(): c for c in df.columns}
        for col in colonnes:
            clé = col.strip().lower()
            if clé in cols_norm:
                colonnes_trouvees.append(cols_norm[clé])
            else:
                colonnes_absentes.append(col)

        # Affichage QTableView avec colonnes sélectionnées
        df_tableview = df[colonnes_trouvees]
        if colonnes_absentes:
            self.afficher_message("Colonnes manquantes ignorées :\n" + "\n".join(colonnes_absentes))
        self.remplir_tableview(self.result_geocode, df_tableview)
        self.afficher_message(f"Chargé : {os.path.basename(dernier_csv)}")

        # Créer la couche point QGIS avec toutes les colonnes
        if "longitude" not in df.columns or "latitude" not in df.columns:
            self.afficher_message("Impossible de créer la couche : longitude ou latitude manquante")
            return

        vl = QgsVectorLayer("Point?crs=EPSG:4326", "Adresses géocodées", "memory")
        pr = vl.dataProvider()

        # Ajouter tous les champs sauf longitude/latitude
        champs = [c for c in df.columns if c not in ["longitude", "latitude"]]
        qfields = QgsFields()
        for c in champs:
            qfields.append(QgsField(c, QVariant.String))
        pr.addAttributes(qfields)
        vl.updateFields()

        # Ajouter les features
        for idx, row in df.iterrows():
            feat = QgsFeature()
            feat.setGeometry(QgsGeometry.fromPointXY(QgsPointXY(float(row["longitude"]), float(row["latitude"]))))
            attrs = [str(row[c]) if c in row else "" for c in champs]
            feat.setAttributes(attrs)
            pr.addFeature(feat)

        vl.updateExtents()
        QgsProject.instance().addMapLayer(vl)
        self.afficher_message("Couche points ajoutée dans QGIS")

        
    # pour remplir la table dans l'interface
    def remplir_tableview(self, tableview, dataframe):
        model = QStandardItemModel()
        model.setRowCount(dataframe.shape[0])
        model.setColumnCount(dataframe.shape[1])
        model.setHorizontalHeaderLabels(list(dataframe.columns))

        for row in range(dataframe.shape[0]):
            for col in range(dataframe.shape[1]):
                item = QStandardItem(str(dataframe.iat[row, col]))
                model.setItem(row, col, item)

        tableview.setModel(model)
        tableview.resizeColumnsToContents()

