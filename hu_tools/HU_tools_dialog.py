class TypeConsoDelegate(QStyledItemDelegate):
    """
    Delegate personnalisé pour les colonnes 'Conso_ret' et 'type_conso'.

    Ce delegate remplace l'éditeur standard (texte) par une liste déroulante.
    Il permet à l'utilisateur de choisir une valeur parmi :
    - "Domestique"
    - "Entreprise"
    - "Faible"
    - à augmenter la liste dès que possible

    Le delegate gère :
    -la création de l'éditeur
    -le chargement de la valeur actuelle dans l'éditeur
    -la sauvegarde de la nouvelle valeur dans le modèle
    - le marquage des cellules modifiées via Qt.UserRole + 1
    """
    def createEditor(self, parent, option, index):
        """
        Appelée lorsque l’utilisateur veut éditer une cellule.
        Ici, on crée un QComboBox à la place d’un champ texte classique.
        """
        combo = QComboBox(parent)            # Création de la liste déroulante
        combo.addItems(["Domestique",        # Valeurs possibles
                        "Entreprise",
                        "Faible",
                        "Service"])
        return combo                   # L'éditeur utilisé dans la cellule

    def setEditorData(self, editor, index):
        """
        Remplissage du QComboBox avec la valeur actuelle de la cellule.
        - On récupère le texte existant dans le modèle.
        - On sélectionne l'entrée correspondante dans la liste.
        """
        value = index.model().data(index, Qt.EditRole)  # Valeur actuelle
        if value:
            pos = editor.findText(value)             # Chercher cette valeur dans le combo
            if pos >= 0:
                editor.setCurrentIndex(pos)             # Sélectionner la valeur existante

    def setModelData(self, editor, model, index):
        """
        Appelée lorsque l’utilisateur valide sa modification.
        - On récupère la valeur choisie dans la liste déroulante.
        - On la renvoie dans le modèle.
        - On marque la cellule comme "modifiée" via Qt.UserRole + 1
          pour que appliquer_modif_table_conso() sache qu’il faut l’enregistrer.
        """
        new_value = editor.currentText()                # Récupération du texte choisi
        model.setData(index, new_value)            # MAJ du modèle
        # Récupérer l'item correspondant
        item = model.item(index.row(), index.column())
        # Marquer comme modifié => appliqué ensuite par appliquer_modif_table_conso()
        item.setData(True, Qt.UserRole + 1)


