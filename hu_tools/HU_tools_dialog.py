            layers = iface.layerTreeView().selectedLayers()
            if not layers:  # Vérification ajoutée
                QMessageBox.critical(
                    None,
                    "Alerte",
                    " Veuillez sélectionner au moins une couche dans le panneau des couches."
                )
                return
            renamed_count = 0

            for layer in layers:
                layer_name = layer.name()
                if suffixe and layer_name.endswith(f"_{suffixe}"):
                    layer_name = layer_name[: -len(f"_{suffixe}")]
                for nca_name, mo_code in name_mapping.items():
                    if nca_name == layer_name:
                        QgsProject.instance().layerTreeRoot().findLayer(layer.id()).setName(mo_code)
                        renamed_count += 1
                        break

            msg = (
                f"{renamed_count} couche(s) ont été renommée(s)." +
                (f"\n(Suffixe retiré : _{suffixe})" if suffixe else "")
                if renamed_count > 0
                else "Aucune couche n'a été renommée."
            )
            QMessageBox.information(self, "Résultat", msg)

