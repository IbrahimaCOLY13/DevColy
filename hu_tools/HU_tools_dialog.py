    # Affiche les statistiques historiques de consommation dans le tableau par ordre chronologique
    def afficher_historique_conso(self):
        """Affiche l'historique cumulatif de consommation."""

        layer_id = self.Couche_geocode_conso.currentData()
        layer = QgsProject.instance().mapLayer(layer_id)
        champ_raccordement = self.Champ_raccordement.currentText()
        critere_raccordement = self.Critere_raccordement.currentText()

        if not layer:
            return

        # Initialiser l'historique si nécessaire
        if not hasattr(self, 'historique_conso'):
            self.historique_conso = {}  # {champ: matrix_data}

        # Récupérer les champs sélectionnés pour cette analyse
        champs_conso_nouveaux = [self.Champs_conso_retenu.currentText()] + self.get_selected_fields()

        # Calculer les stats pour chaque nouveau champ et stocker dans l'historique
        for champ in champs_conso_nouveaux:
            if champ in self.historique_conso:
                continue  # déjà calculé

            # indices
            idx_raccordement = layer.fields().indexOf(champ_raccordement)
            idx_conso = layer.fields().indexOf(champ)

            # Indicateurs
            indicateurs = [
                "Nombre total d'abonnés assainis",
                "Consommation totale (m³)",
                "Nombre d'abonnés domestiques",
                "Consommation domestique (m³)",
                "Consommation moyenne domestique (m³/ab/an)",
                "Consommation journalière par habitant (L/j/hab)",
                "Nombre d'abonnés non domestiques",
                "Consommation non domestique (m³)",
                "Consommation moyenne non domestique (m³/ab/an)"
            ]

            stats = {ind: 0 for ind in indicateurs}
            ratio_hab = float(self.Ratio_hab.toPlainText() or "2.5")

            for feature in layer.getFeatures():
                raccordement_value = str(feature[idx_raccordement]) if feature[idx_raccordement] else ""
                if critere_raccordement and raccordement_value != critere_raccordement:
                    continue
                #if raccordement_value != critere_raccordement:
                #    continue
                try:
                    conso_value = float(feature[idx_conso]) if feature[idx_conso] else 0
                    if conso_value <= 5:
                        continue
                    type_conso = feature['type_conso'] if feature['type_conso'] else ""
                    stats["Nombre total d'abonnés assainis"] += 1
                    stats["Consommation totale (m³)"] += conso_value
                    if type_conso == 'Domestique':
                        stats["Nombre d'abonnés domestiques"] += 1
                        stats["Consommation domestique (m³)"] += conso_value
                    else:
                        stats["Nombre d'abonnés non domestiques"] += 1
                        stats["Consommation non domestique (m³)"] += conso_value
                except:
                    continue

            # Moyennes
            stats["Consommation moyenne domestique (m³/ab/an)"] = (
                stats["Consommation domestique (m³)"] / stats["Nombre d'abonnés domestiques"]
                if stats["Nombre d'abonnés domestiques"] > 0 else 0
            )
            stats["Consommation moyenne non domestique (m³/ab/an)"] = (
                stats["Consommation non domestique (m³)"] / stats["Nombre d'abonnés non domestiques"]
                if stats["Nombre d'abonnés non domestiques"] > 0 else 0
            )
            stats["Consommation journalière par habitant (L/j/hab)"] = (
                (stats["Consommation moyenne domestique (m³/ab/an)"]*1000)/(ratio_hab*365)
                if ratio_hab>0 else 0
            )

            # Ajouter au dictionnaire historique
            self.historique_conso[champ] = stats

        # Trier les champs par année
        import re
        def extraire_annee(champ):
            m = re.search(r'(\d{4})', champ)
            return int(m.group(1)) if m else 0

        champs_triees = sorted(self.historique_conso.keys(), key=extraire_annee)

        # Remplir le tableau
        self.conso_model.clear()
        headers = ['Indicateur'] + champs_triees
        self.conso_model.setHorizontalHeaderLabels(headers)
        indicateurs = list(next(iter(self.historique_conso.values())).keys())

        for indicateur in indicateurs:
            row_items = [QStandardItem(indicateur)]
            for champ in champs_triees:
                row_items.append(QStandardItem(str(round(self.historique_conso[champ][indicateur],1))))
            self.conso_model.appendRow(row_items)

        self.apercu_conso.resizeColumnsToContents()

        
