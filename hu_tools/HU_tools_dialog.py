    # Charge automatiquement les couches dans les listes déroulantes en fonction du fichier correspondant au MO choisi.
    def charger_couches_par_mo(self, mo):
        """
        Ex: mo='EDV' -> Nom_couche_EDV_Verif_Topo.csv
        """
        # Nettoyage robuste du MO (supprimer BOM et caractères invisibles)
        if mo is None:
            return
        mo = str(mo).strip()
        mo = mo.replace('\ufeff', '').replace('\u200b', '').strip()

        # Construire le nom exact du fichier attendu
        csv_filename = f"Nom_couche_{mo}_Verif_Topo.csv"
        table_dir = os.path.join(os.path.dirname(__file__), 'table')
        csv_path = os.path.join(table_dir, csv_filename)

        # Fallback si nécessaire (comme déjà fait)
        if not os.path.exists(csv_path):
            candidates = []
            try:
                for fn in os.listdir(table_dir):
                    fn_clean = fn.replace('\ufeff', '').lower()
                    if f"_{mo.lower()}_verif_topo" in fn_clean:
                        candidates.append(fn)
            except Exception:
                candidates = []

            if candidates:
                csv_filename = candidates[0]
                csv_path = os.path.join(table_dir, csv_filename)
            else:
                QMessageBox.warning(
                    self,
                    "Fichier introuvable",
                    f"Fichier attendu introuvable :\n{csv_filename}\n\nVérifiez que le fichier existe dans {table_dir}."
                )
                return

        # Liste des combobox à gérer
        combobox_names = [
            "topo_res_eu", "topo_res_ep", "topo_res_uni", "topo_refoulement",
            "topo_brch_eu", "topo_brch_ep", "topo_brch_uni", "topo_fosse",
            "topo_do", "topo_reg_eu", "topo_reg_uni", "topo_reg_ep",
            "topo_PR", "topo_bt_eu", "topo_bt_uni", "topo_bt_ep",
            "topo_avaloir", "topo_exutoire", "topo_grille"
        ]

        # Construire dict default { combo_name: default_layer } à partir du CSV
        defaults = {}
        try:
            with open(csv_path, 'r', encoding='utf-8-sig') as f:
                header_line = f.readline()
                if not header_line:
                    return

                header = [h.strip().replace('\ufeff', '') for h in header_line.strip().split(";")]
                header_lower = [h.lower() for h in header]

                # trouver indices (tolérant à la casse / accents)
                try:
                    idx_code_edv = header_lower.index("code_edv")
                except ValueError:
                    QMessageBox.warning(self, "Erreur CSV", f"Colonne 'Code_EDV' introuvable dans {csv_filename}.\nColonnes: {header}")
                    return
                try:
                    idx_combobox = header_lower.index("codes_listes_déroulantes")
                except ValueError:
                    # essayer variante sans accent
                    try:
                        idx_combobox = header_lower.index("codes_listes_deroulantes")
                    except ValueError:
                        QMessageBox.warning(self, "Erreur CSV", f"Colonne 'Codes_Listes_Déroulantes' introuvable dans {csv_filename}.\nColonnes: {header}")
                        return

                for line in f:
                    if not line.strip():
                        continue
                    parts = [p.strip() for p in line.strip().split(";")]
                    if len(parts) <= max(idx_code_edv, idx_combobox):
                        continue

                    code_edv = parts[idx_code_edv]
                    combo_name = parts[idx_combobox]

                    if code_edv and combo_name:
                        defaults[combo_name] = code_edv
        except Exception as e:
            QMessageBox.warning(self, "Erreur", f"Lecture du fichier {csv_filename} impossible : {str(e)}")
            return

        # Récupérer tous les noms de couches actuellement dans le projet (liste simple)
        all_layers = QgsProject.instance().mapLayers().values()
        all_layer_names = [layer.name() for layer in all_layers]

        # Vider les listes et remplir avec options (default en premier, puis les autres)
        for name in combobox_names:
            combobox = getattr(self, name, None)
            if combobox is None:
                continue

            # Bloquer signaux pendant le remplissage
            combobox.blockSignals(True)
            combobox.clear()
            combobox.setEnabled(True)         # s'assurer qu'elle est activée
            combobox.setEditable(False)       # si tu veux permettre saisie manuelle => True

            # Préparer la liste d'options : default (si présent), puis autres couches (sans doublons)
            options = []
            default_vals = defaults.get(name, [])
            matched_layer = None
            
            # Rechercher si une couche du projet correspond au Code_EDV ou Nom NCA
            if default_vals:
                for lname in all_layer_names:
                    if lname in default_vals:
                        options.append(lname)
                        if matched_layer is None:
                            matched_layer = lname  # Garder la première correspondance pour la sélection
                        break
                                                               

            # Ajouter toutes les autres couches disponibles (sans doublons)
            for lname in all_layer_names:
                if lname not in options:
                    options.append(lname)

            # Ajouter les options dans la combobox
            for opt in options:
                combobox.addItem(opt)

            # Positionner la valeur courante sur la couche correspondante si trouvée
            if matched_layer and matched_layer in options:
                index = options.index(matched_layer)
                combobox.setCurrentIndex(index)
            else:
                # sinon, on peut laisser l'index sur -1 ou 0 ; ici on laisse 0 s'il y a des options
                if options:
                    combobox.setCurrentIndex(0)
            combobox.blockSignals(False)
