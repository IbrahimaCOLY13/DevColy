    #1) Vérification des connexions
    def verify_topology(self):
        """Lance la vérification topologique selon les options cochées."""
        # Récupérer toutes les couches sélectionnées dans les combobox
        layers = self.get_selected_topology_layers()
        
        if not layers:
            QtWidgets.QMessageBox.warning(
                self,
                "Aucune couche sélectionnée", 
                "Veuillez sélectionner au moins une couche pour la vérification topologique."
            )
            return
            
        # Récupérer le MO sélectionné
        mo = self.Topo_Mo.currentText().strip()
        if not mo:
            mo = 'EDV'  # Valeur par défaut
        
        # Initialiser la liste des logs
        self.topology_logs = []
        
        # Fonction callback pour les logs
        def log_callback(message):
            """Envoie les messages de log vers le QWebView"""
            self.topology_logs.append(message)
            print(f"[TOPO] {message}")  # Afficher aussi dans la console Python
            self.update_topology_log()
        
        # Créer l'instance du vérificateur de topologie avec le MO et le callback de log
        # Tolérance de connexion : 0.1m (10cm) pour être moins restrictif
        checker = TopologyChecker(layers, connection_tolerance=0.1, mo=mo, log_callback=log_callback)
        
        # Afficher un dialogue de progression
        progress = QtWidgets.QProgressDialog("Vérification de la topologie en cours...", "Annuler", 0, 9, self)
        progress.setWindowTitle("Vérification topologique")
        progress.setWindowModality(Qt.WindowModal)
        progress.setValue(0)
        progress.setMinimumDuration(0)
        progress.show()
        QtWidgets.QApplication.processEvents()  # Forcer le rafraîchissement
        
        # Exécuter toutes les vérifications
        try:
            # Stocker toutes les erreurs pour pouvoir les filtrer plus tard
            self.all_topology_errors = []
            
            # Vérifier les connexions entre boîtes et branchements
            progress.setLabelText("Vérification des connexions boîtes-branchements...")
            progress.setValue(1)
            QtWidgets.QApplication.processEvents()                                                 
            if progress.wasCanceled():
                return
            box_branch_errors = checker.check_connections_boxes_branches()
            self.all_topology_errors.extend(box_branch_errors)
            
            # Vérifier les connexions entre branchements et réseau
            progress.setLabelText("Vérification des connexions branchements-réseau...")
            progress.setValue(2)
            QtWidgets.QApplication.processEvents()                                      
            if progress.wasCanceled():
                return
            branch_network_errors = checker.check_connections_branches_network()
            self.all_topology_errors.extend(branch_network_errors)
            
            # Vérifier les connexions entre regards et canalisations
            progress.setLabelText("Vérification des connexions regards-canalisations...")
            progress.setValue(3)
            QtWidgets.QApplication.processEvents()                                      
            if progress.wasCanceled():
                return
            manhole_pipe_errors = checker.check_connections_manholes_pipes()
            self.all_topology_errors.extend(manhole_pipe_errors)
            
            # Vérifier les connexions entre DO et canalisations
            progress.setLabelText("Vérification des connexions DO-canalisation...")
            progress.setValue(4)
            QtWidgets.QApplication.processEvents()                                      
            if progress.wasCanceled():
                return
            do_pipe_errors = checker.check_connections_do_pipes()
            self.all_topology_errors.extend(do_pipe_errors)
            
            # Vérifier les connexions entre PR, refoulement et regards
            progress.setLabelText("Vérification des connexions refoulement-PR-regard...")
            progress.setValue(5)
            QtWidgets.QApplication.processEvents()                                      
            if progress.wasCanceled():
                return
            pr_pipe_errors = checker.check_connections_pr_pipes()
            self.all_topology_errors.extend(pr_pipe_errors)
            
            # Vérifier les superpositions d'entités
            progress.setLabelText("Vérification des superpositions d'entités...")
            progress.setValue(6)
            QtWidgets.QApplication.processEvents()                                      
            if progress.wasCanceled():
                return
            overlap_errors = checker.check_entity_overlaps()
            self.all_topology_errors.extend(overlap_errors)
            
            # Vérifier la cohérence des altitudes
            progress.setLabelText("Vérification de la cohérence des altitudes...")
            progress.setValue(7)
            QtWidgets.QApplication.processEvents()                                      
            if progress.wasCanceled():
                return
            coherence_altitudes_erreur = checker.check_coherence_altitudes()
            self.all_topology_errors.extend(coherence_altitudes_erreur)
            
            # Vérifier le sens d'écoulement (peut être long sur les grands réseaux)
            progress.setLabelText("Vérification du sens d'écoulement (cette étape peut prendre plusieurs minutes)...")
            progress.setValue(8)
            QtWidgets.QApplication.processEvents()                                      
            if progress.wasCanceled():
                return
            flow_direction_errors = checker.check_flow_direction()
            self.all_topology_errors.extend(flow_direction_errors)
            QtWidgets.QApplication.processEvents()
            
            # Consolider les erreurs (fusionner les doublons)
            progress.setLabelText("Consolidation des erreurs...")
            progress.setValue(9)
            QtWidgets.QApplication.processEvents()
            
            # Log avant consolidation
            flow_errors_before = sum(1 for e in self.all_topology_errors if e.get('type') == 'Sens d\'écoulement')
            self.topology_logs.append(f"[CONSOLIDATION] Erreurs totales avant: {len(self.all_topology_errors)}")
            self.topology_logs.append(f"[CONSOLIDATION] Erreurs 'Sens d'écoulement' avant: {flow_errors_before}")
            self.update_topology_log()
            
            self.all_topology_errors = checker.consolidate_errors(self.all_topology_errors)
            
            # Log après consolidation
            flow_errors_after = sum(1 for e in self.all_topology_errors if 'Sens d\'écoulement' in str(e.get('type', '')))
            self.topology_logs.append(f"[CONSOLIDATION] Erreurs totales après: {len(self.all_topology_errors)}")
            self.topology_logs.append(f"[CONSOLIDATION] Erreurs 'Sens d'écoulement' après: {flow_errors_after}")
            self.topology_logs.append(f"[CONSOLIDATION] ⚠️ PERTE de {len(self.all_topology_errors) + flow_errors_before - len(self.all_topology_errors) - flow_errors_after} erreurs lors de la fusion!")
            self.update_topology_log()
            
            # Fermer la fenêtre de progression
            progress.close()                                      
            # Filtrer les erreurs selon les options cochées pour l'affichage
            filtered_errors = self.filter_errors_by_selected_checks(self.all_topology_errors)
            
            # Afficher les erreurs dans le tableau
            self.display_errors_in_table(filtered_errors)
            
            # Créer un résumé des logs pour le debug
            log_summary = "\n".join(self.topology_logs)
            
            # Informer l'utilisateur avec option d'afficher les logs
            msg_box = QtWidgets.QMessageBox(self)
                     
            msg_box.setWindowTitle("Vérification terminée")
            msg_box.setText(f"{len(filtered_errors)} erreurs topologiques affichées sur un total de {len(self.all_topology_errors)} détectées.")
            msg_box.setDetailedText(log_summary)
            msg_box.setIcon(QtWidgets.QMessageBox.Information)
            msg_box.exec_()
                 
        except Exception as e:
            import traceback
            error_details = traceback.format_exc()
            QtWidgets.QMessageBox.critical(
                self,
                "Erreur lors de la vérification", 
                f"Une erreur est survenue lors de la vérification topologique:\n{str(e)}\n\nDétails: {error_details}"
            )

