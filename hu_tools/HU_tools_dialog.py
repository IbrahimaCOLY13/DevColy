    def create_vector_layer_from_geocoded_csv(self, csv_file):
        """Crée une couche vectorielle temporaire à partir du CSV géocodé."""
        try:
            # Créer un transformateur de coordonnées de WGS84 vers Lambert-93
            source_crs = QgsCoordinateReferenceSystem("EPSG:4326")
            dest_crs = QgsCoordinateReferenceSystem("EPSG:2154")
            transform = QgsCoordinateTransform(source_crs, dest_crs, QgsProject.instance())
    
            # Créer un GeoJSON temporaire
            features = []
            with open(csv_file, 'r', encoding='utf-8') as f:
                reader = csv.DictReader(f)
                for row in reader:
                    # Vérifier si les coordonnées sont présentes
                    if 'result_longitude' in row and 'result_latitude' in row and row['result_longitude'] and row['result_latitude']:
                        try:
                            lon = float(row['result_longitude'])
                            lat = float(row['result_latitude'])
                            
                            # Transformer les coordonnées de WGS84 vers Lambert-93
                            point = QgsPointXY(lon, lat)
                            transformed_point = transform.transform(point)
                            
                            feature = {
                                "type": "Feature",
                                "geometry": {
                                    "type": "Point",
                                    "coordinates": [transformed_point.x(), transformed_point.y()]
                                },
                                "properties": {k: v for k, v in row.items()}
                            }
                            features.append(feature)
                        except (ValueError, TypeError):
                            # Ignorer les lignes avec des coordonnées invalides
                            continue
    
            if not features:
                QMessageBox.warning(self, "Avertissement", "Aucune adresse géocodée avec des coordonnées valides.")
                return
    
            # Créer le GeoJSON
            geojson = {
                "type": "FeatureCollection",
                "features": features
            }
            
            # Sauvegarder le GeoJSON temporaire
            output_geojson = os.path.splitext(csv_file)[0] + '.geojson'
            with open(output_geojson, 'w', encoding='utf-8') as f:
                json.dump(geojson, f, ensure_ascii=False)
            
            # Créer et ajouter la couche à QGIS
            layer = QgsVectorLayer(output_geojson, "Adresses géocodées", "ogr")
            
            if layer.isValid():
                # Définir le système de coordonnées (Lambert-93)
                crs = QgsCoordinateReferenceSystem("EPSG:2154")
                layer.setCrs(crs)
                
                # Ajouter la couche au projet QGIS
                QgsProject.instance().addMapLayer(layer)
                
                # Créer une symbologie simple
                symbol = QgsMarkerSymbol.createSimple({
                    'name': 'circle',
                    'color': '255,0,0,255',
                    'size': '3',
                    'outline_color': '0,0,0,255',
                    'outline_width': '0.4'
                })
                layer.renderer().setSymbol(symbol)
                layer.triggerRepaint()
            else:
                QMessageBox.warning(self, "Erreur", "Impossible de créer une couche valide à partir des données géocodées.")
                
        except Exception as e:
            QMessageBox.critical(self, "Erreur", f"Erreur lors de la création de la couche : {str(e)}")
            
