    def deplacer_etiquette_FE(self, dx_units, dy_units):
        if not self.PL_etiquette_FE_placement.isChecked():
            return
        step = self._calcul_pas()
        # Correction : on modifie l'instance de ProfilePlotter
        self.profile_plotter.offset_FE_x += dx_units * step
        self.profile_plotter.offset_FE_y += dy_units * step
        # Redessine uniquement (ne régénère pas les données)
        self.profile_plotter.redraw_profile()

    def deplacer_etiquette_TN(self, dx_units, dy_units):
        if not self.PL_etiquette_TN_placement.isChecked():
            return
        step = self._calcul_pas()
        # Correction : même principe pour TN
        self.profile_plotter.offset_TN_x += dx_units * step
        self.profile_plotter.offset_TN_y += dy_units * step
        self.profile_plotter.redraw_profile()

        
    def _calcul_pas(self):
        """Calcule un pas de déplacement adapté à l’échelle du profil."""
        try:
            span = max(1e-6, self.max_x - self.min_x)
        except:
            span = 100.0
        return span * 0.015   # 1.5% de la largeur


