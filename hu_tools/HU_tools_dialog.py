    #ça permet d'appliquer toutes les modifications effectuées dans la table de consommation à la couche QGIS correspondante
    def appliquer_modif_table_conso(self):
        layer_id = self.Couche_geocode_conso.currentData()
        layer = QgsProject.instance().mapLayer(layer_id)
        if not layer:
            QMessageBox.warning(self, "Erreur", "Aucune couche sélectionnée.")
            return

        if not layer.isEditable():
            layer.startEditing()

        fields = layer.fields()
        modifications = 0

        for row in range(self.conso_model.rowCount()):
            for col in range(self.conso_model.columnCount()):

                item = self.conso_model.item(row, col)
                if not item:
                    continue

                # pou Vérifie si l'utilisateur a modifié cette cellule
                if not item.data(Qt.UserRole + 1):
                    continue

                feature_id = item.data(Qt.UserRole)
                if feature_id is None:
                    continue

                field = fields[col]
                nouvelle_valeur_str = item.text()

                if field.type() in (QVariant.Int, QVariant.LongLong):
                    try:
                        nouvelle_valeur = int(nouvelle_valeur_str)
                    except:
                        nouvelle_valeur = None

                elif field.type() == QVariant.Double:
                    try:
                        nouvelle_valeur = float(nouvelle_valeur_str.replace(",", "."))
                    except:
                        nouvelle_valeur = None

                else:
                    nouvelle_valeur = nouvelle_valeur_str

                ok = layer.changeAttributeValue(feature_id, col, nouvelle_valeur)
                if ok:
                    modifications += 1

                    # Reset du flag
                    item.setData(False, Qt.UserRole + 1)

        if modifications > 0:
            layer.commitChanges()
            QMessageBox.information(
                self,
                "Modifications appliquées",
                f"{modifications} modification(s) appliquée(s) à la couche “{layer.name()}”."
            )
        else:
            layer.rollBack()
            QMessageBox.information(
                self,
                "Aucune modification",
                "Aucune cellule modifiée."
            )


    def calculer_debits_BV(self):
        """Calcule les débits pour chaque bassin versant en tenant compte du critère de raccordement."""
        # Vérifier que les couches sont sélectionnées
        if self.Couche_BV_Conso_AEP.currentIndex() < 0 or self.Couche_geocode_conso.currentIndex() < 0:
            QMessageBox.warning(self, "Attention", "Veuillez sélectionner une couche BV et une couche de consommation.")
            return

        # Obtenir les couches
        bv_layer_id = self.Couche_BV_Conso_AEP.currentData()
        conso_layer_id = self.Couche_geocode_conso.currentData()
        bv_layer = QgsProject.instance().mapLayer(bv_layer_id)
        conso_layer = QgsProject.instance().mapLayer(conso_layer_id)
        
        if not bv_layer or not conso_layer:
            return
            
        # Obtenir le ratio et les critères de raccordement
        ratio = float(self.Ratio_conso.toPlainText() or "0.8")
        champ_raccordement = self.Champ_raccordement.currentText()
        critere_raccordement = self.Critere_raccordement.currentText()
        
        # Vérifier si les champs existent déjà dans la couche BV
        champs_a_creer = {}
        for nom_champ in ['Q_EU_DOM', 'Q_EU_ENT', 'Q_EU_SER']:
            field = QgsField(nom_champ, QVariant.Double)
            field.setLength(10)
            field.setPrecision(3)
            champs_a_creer[nom_champ] = field
        
        # Commencer l'édition de la couche BV
        if not bv_layer.isEditable():
            bv_layer.startEditing()
            
        # Créer les champs s'ils n'existent pas
        for nom_champ, field in champs_a_creer.items():
            if bv_layer.fields().indexOf(nom_champ) == -1:
                bv_layer.addAttribute(field)
                
        # Obtenir les indices des champs
        idx_dom = bv_layer.fields().indexOf('Q_EU_DOM')
        idx_ent = bv_layer.fields().indexOf('Q_EU_ENT')
        idx_ser = bv_layer.fields().indexOf('Q_EU_SER')
        idx_type_conso = conso_layer.fields().indexOf('type_conso')
        idx_conso = conso_layer.fields().indexOf('Conso_ret')  # Utiliser Conso_ret au lieu du champ sélectionné
        idx_raccordement = conso_layer.fields().indexOf(champ_raccordement)
        
        # Vérifier si le champ Conso_ret existe
        if idx_conso == -1:
            QMessageBox.warning(self, "Attention", "Veuillez d'abord effectuer un pré-tri des consommations.")
            return
        
        # Liste ou compteur des BV sans intersection
        bv_sans_intersection = []

        # Pour chaque bassin versant
        for bv_feat in bv_layer.getFeatures():
            # Initialiser les sommes
            sum_dom = 0
            sum_ent = 0
            sum_ser = 0
            intersection_trouvee = False  
            
            # Sélectionner les points de consommation qui intersectent avec le BV
            for conso_feat in conso_layer.getFeatures():
                if conso_feat.geometry().intersects(bv_feat.geometry()):
                    intersection_trouvee = True  # On a trouvé au moins une intersection
                    try:
                        # Vérifier le critère de raccordement
                        raccordement_value = str(conso_feat[idx_raccordement]) if conso_feat[idx_raccordement] else ""
                        if critere_raccordement and raccordement_value != critere_raccordement:
                            continue
                            
                        conso_value = float(conso_feat[idx_conso]) if conso_feat[idx_conso] else 0
                        type_conso = conso_feat[idx_type_conso]
                        
                        if type_conso == 'Domestique':
                            sum_dom += conso_value
                        elif type_conso == 'Entreprise':
                            sum_ent += conso_value
                        elif type_conso == 'Service':
                            sum_ser += conso_value
                    except (ValueError, TypeError):
                        continue
            
            # Ajouter le BV à la liste s'il n'y a aucune intersection
            if not intersection_trouvee:
                bv_sans_intersection.append(bv_feat.id())
                        
            # Calculer les débits
            q_dom = (sum_dom / 365.0) * ratio if sum_dom > 0 else 0
            q_ent = (sum_ent / 200.0) * ratio if sum_ent > 0 else 0
            q_ser = (sum_ser / 200.0) * ratio if sum_ser > 0 else 0
            
            # Mettre à jour les champs
            bv_layer.changeAttributeValue(bv_feat.id(), idx_dom, q_dom)
            bv_layer.changeAttributeValue(bv_feat.id(), idx_ent, q_ent)
            bv_layer.changeAttributeValue(bv_feat.id(), idx_ser, q_ser)
        
        # Afficher le message une seule fois
        if bv_sans_intersection:
            QMessageBox.information(
                self,
                "Avertissement",
                f"{len(bv_sans_intersection)} bassin(s) versant n'ont aucun point de consommation intersectant."
            )
    
        # Sauvegarder les modifications
        if bv_layer.commitChanges():
            message = "Les débits ont été calculés et mis à jour avec succès"
            if critere_raccordement:
                message += f" pour {champ_raccordement}={critere_raccordement}"
            QMessageBox.information(self, "Succès", message)
            bv_layer.triggerRepaint()
        else:
            QMessageBox.warning(self, "Erreur", "Impossible de sauvegarder les modifications.")
            bv_layer.rollBack()

    def zoom_sur_selection(self):
        """Zoom sur l'entité sélectionnée dans apercu_conso à l'échelle 1:1000."""
        selected_indexes = self.apercu_conso.selectionModel().selectedRows()
        if not selected_indexes:
            QMessageBox.warning(self, "Attention", "Veuillez sélectionner une entité dans le tableau.")
            return

        # Récupérer la couche sélectionnée
        layer_id = self.Couche_geocode_conso.currentData()
        layer = QgsProject.instance().mapLayer(layer_id)
        if not layer:
            QMessageBox.warning(self, "Erreur", "Impossible de trouver la couche sélectionnée.")
            return

        # Récupérer l'ID de l'entité
        selected_row = selected_indexes[0].row()
        feature_id = self.conso_model.item(selected_row, 0).data(Qt.UserRole)

        # Charger l'entité
        request = QgsFeatureRequest().setFilterFid(feature_id)
        feature = next(layer.getFeatures(request), None)

        if not feature or not feature.geometry():
            QMessageBox.warning(self, "Erreur", "Impossible de trouver la géométrie de l'entité sélectionnée.")
            return

        geom = feature.geometry()
        if geom.isEmpty():
            QMessageBox.warning(self, "Erreur", "La géométrie de l'entité est vide.")
            return

        # Centrer la carte sur l'entité (en respectant la projection du canevas)
        canvas = iface.mapCanvas()
        layer_crs = layer.crs()
        canvas_crs = canvas.mapSettings().destinationCrs()

        # Reprojeter si nécessaire
        if layer_crs != canvas_crs:
            transform = QgsCoordinateTransform(layer_crs, canvas_crs, QgsProject.instance())
            geom = QgsGeometry(geom)
            geom.transform(transform)

        # Zoom sur l’emprise (plus fiable que centroid pour les polygones ou lignes)
        rect = geom.boundingBox()
        if rect.isNull():
            QMessageBox.warning(self, "Erreur", "Impossible de déterminer l'étendue de l'entité.")
            return

        # Zoom et centrage
        canvas.setExtent(rect)
        canvas.zoomScale(1000)
        canvas.refresh()

        # Sélectionner l'entité dans la couche
        layer.removeSelection()
        layer.select(feature_id)

