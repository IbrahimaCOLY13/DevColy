    def display_errors_in_table(self, errors):
        """Affiche les erreurs dans la table des erreurs."""
        # Vider le tableau
        self.topo_table_erreurs.clearContents()
        self.topo_table_erreurs.setRowCount(0)
        
        # Vérifier les types d'erreurs à afficher selon les cases cochées
        show_box_branch = self.check_conn_bt.isChecked()
        show_branch_network = self.check_conn_brc.isChecked()
        show_manhole_pipe = self.check_conn_reg.isChecked()
        show_do_pipe = self.check_conn_do.isChecked()
        show_pr_pipe = self.check_conn_pr.isChecked()
        show_overlaps = self.check_superposition.isChecked()
        verif_altitude = self.check_altitudes.isChecked()
        show_flow_direction = self.check_sens_ecoul.isChecked()
        
        # Filtrer les erreurs selon les cases cochées
        filtered_errors = []
        for error in errors:
            error_type = error['type']
            
            if ((error_type == 'Connexion boîtes-branchements' and show_box_branch) or
                (error_type == 'Connexion branchements-réseau' and show_branch_network) or
                (error_type == 'Connexion regards-canalisations' and show_manhole_pipe) or
                (error_type == 'Connexion DO-canalisation' and show_do_pipe) or
                (error_type == 'Connexion refoulement-PR-regard' and show_pr_pipe) or
                (error_type == 'Superposition d\'entités' and show_overlaps) or
                (error_type == 'Incohérence altitude amont' and verif_altitude) or
                (error_type == 'Incohérence altitude aval' and verif_altitude) or
                (error_type == 'Altitude manquante' and verif_altitude) or
                (error_type == 'Champ manquant' and verif_altitude) or
                (error_type == 'Sens d\'écoulement' and show_flow_direction)):
                filtered_errors.append(error)
        
        # Configurer le tableau
        self.topo_table_erreurs.setRowCount(len(filtered_errors))
        
        # Remplir le tableau avec les erreurs filtrées
        for row, error in enumerate(filtered_errors):
            # Colonne Type
            self.topo_table_erreurs.setItem(row, 0, QTableWidgetItem(error['type']))
            # Colonne Description
            self.topo_table_erreurs.setItem(row, 1, QTableWidgetItem(error['description']))
            # Colonne Couche
            self.topo_table_erreurs.setItem(row, 2, QTableWidgetItem(error['layer']))
            # Colonne ID
            self.topo_table_erreurs.setItem(row, 3, QTableWidgetItem(str(error['feature_id'])))
            
            # Stocker la géométrie pour le zoom (peut être None pour les erreurs de configuration)
            geometry = error.get('geometry', None)
            self.topo_table_erreurs.item(row, 0).setData(Qt.UserRole, geometry)
        
        # Mettre à jour les options des filtres
        self.update_column_filter_options()
        
        # Appliquer les filtres de colonnes si nécessaire
        self.apply_column_filters()
    
    def setup_column_filters(self):
        """Initialise les filtres de colonnes en utilisant les combobox du .ui"""
        # Connecter les combobox du .ui aux filtres
        self.topo_filter_type.currentIndexChanged.connect(self.apply_column_filters)
        self.topo_filter_couche.currentIndexChanged.connect(self.apply_column_filters)
        print("✓ Filtres topologiques connectés")
    
    def update_column_filter_options(self):
        """Met à jour les options disponibles dans les filtres de colonnes"""
        # Récupérer les valeurs uniques pour Type (colonne 0)
        types_unique = set()
        for row in range(self.topo_table_erreurs.rowCount()):
            item = self.topo_table_erreurs.item(row, 0)
            if item:
                types_unique.add(item.text())
        
        # Mettre à jour le combobox Type
        current_type = self.topo_filter_type.currentText()
        self.topo_filter_type.blockSignals(True)
        self.topo_filter_type.clear()
        self.topo_filter_type.addItem("-- Tous --")
        for value in sorted(types_unique):
            self.topo_filter_type.addItem(value)
        index = self.topo_filter_type.findText(current_type)
        if index >= 0:
            self.topo_filter_type.setCurrentIndex(index)
        self.topo_filter_type.blockSignals(False)
        
        # Récupérer les valeurs uniques pour Couche (colonne 2)
        couches_unique = set()
        for row in range(self.topo_table_erreurs.rowCount()):
            item = self.topo_table_erreurs.item(row, 2)
            if item:
                couches_unique.add(item.text())
        
        # Mettre à jour le combobox Couche
        current_couche = self.topo_filter_couche.currentText()
        self.topo_filter_couche.blockSignals(True)
        self.topo_filter_couche.clear()
        self.topo_filter_couche.addItem("-- Tous --")
        for value in sorted(couches_unique):
            self.topo_filter_couche.addItem(value)
        index = self.topo_filter_couche.findText(current_couche)
        if index >= 0:
            self.topo_filter_couche.setCurrentIndex(index)
        self.topo_filter_couche.blockSignals(False)
    
    def apply_column_filters(self):
        """Applique les filtres de colonnes au tableau"""
        filter_type = self.topo_filter_type.currentText()
        filter_couche = self.topo_filter_couche.currentText()
        
        # Pour chaque ligne, vérifier si elle correspond aux filtres
        for row in range(self.topo_table_erreurs.rowCount()):
            show_row = True
            
            # Filtre Type (colonne 0)
            if filter_type != "-- Tous --":
                item = self.topo_table_erreurs.item(row, 0)
                if item and item.text() != filter_type:
                    show_row = False
            
            # Filtre Couche (colonne 2)
            if show_row and filter_couche != "-- Tous --":
                item = self.topo_table_erreurs.item(row, 2)
                if item and item.text() != filter_couche:
                    show_row = False
            
            # Afficher ou masquer la ligne
            self.topo_table_erreurs.setRowHidden(row, not show_row)
