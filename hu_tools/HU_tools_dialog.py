    def _calculer_repartition_rr1(self):
        """
        Calcule la répartition RR1_xh / RR1_total * 100
        Retourne un dict :
        {
            "RR1_2h": [(ratio%, valeur_mm), ...],
            "RR1_6h": [...],
            "RR1_24h": [...]
        }
        """
        if not hasattr(self, "_buffer_resultats_complet"):
            return {}
        data = self._buffer_resultats_complet
        # Somme RR1 totale
        total = 0.0
        for r in data:
            val = self._to_float_rr(r.get("RR1"))
            if val:
                total += val
        if total <= 0:
            return {}

        repartition = {}
        for name, _ in self.WINDOWS:
            points = []
            for r in data:
                val = self._to_float_rr(r.get(name))
                if val is None or val <= 0:
                    continue
                ratio = (val / total) * 100.0
                points.append((ratio, val))
            repartition[name] = points
        return repartition
    
        
