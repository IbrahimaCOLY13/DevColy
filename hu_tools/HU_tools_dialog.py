    #pour remplace toutes les valeurs de 'result_name' par celles de la première colonne (champ initial)
    def remplacer_result_name(self):
        """
        Remplace les valeurs de 'result_name' par celles de la première colonne,
        uniquement pour les lignes sélectionnées.
        Met à jour uniquement les lignes modifiées dans le CSV, la couche QGIS,
        et ajoute automatiquement une colonne 'modifie' = 1 pour signaler le re-géocodage.
        """
        try:
            # Vérifier qu'une sélection existe
            selection = self.result_geocode.selectionModel()
            if not selection or not selection.hasSelection():
                QMessageBox.warning(self, "Attention", "Veuillez sélectionner au moins une ligne.")
                return

            # Récupération du modèle
            proxy_modele = self.result_geocode.model()
            modele = proxy_modele.sourceModel() if hasattr(proxy_modele, "sourceModel") else proxy_modele

            # --- Chargement du CSV 
            import pandas as pd
            dossier_temp = os.path.join(os.path.dirname(__file__), 'temp')
            mauvais_csv = os.path.join(dossier_temp, 'geocoded_mauvais_score.csv')

            if not os.path.exists(mauvais_csv):
                QMessageBox.critical(self, "Erreur", f"Fichier introuvable :\n{mauvais_csv}")
                return

            df_mauvais = pd.read_csv(mauvais_csv, encoding='utf-8-sig')

            # Ajouter colonne modifie si absente
            if "modifie" not in df_mauvais.columns:
                df_mauvais["modifie"] = 0

            #  Récupération couche QGIS 
            couche_geocoded = None
            for couche in QgsProject.instance().mapLayers().values():
                if couche.name() == "Adresses géocodées" and isinstance(couche, QgsVectorLayer):
                    couche_geocoded = couche
                    break

            if not couche_geocoded or not couche_geocoded.isValid():
                QMessageBox.warning(self, "Erreur", "Couche 'Adresses géocodées' introuvable.")
                return

            # Mode édition
            edition_locale = False
            if not couche_geocoded.isEditable():
                couche_geocoded.startEditing()
                edition_locale = True

            index_result_name = couche_geocoded.fields().indexOf('result_name')
            compteur_modifications = 0

            #  Traitement des lignes sélectionnées 
            for index in selection.selectedRows():
                indice_ligne_csv = index.row()

                ancienne_valeur = str(modele.data(modele.index(indice_ligne_csv, 1)))
                nouvelle_valeur = str(modele.data(modele.index(indice_ligne_csv, 0)))

                if not nouvelle_valeur or not ancienne_valeur:
                    continue

                # MAJ dans le CSV
                df_mauvais.loc[indice_ligne_csv, "result_name"] = nouvelle_valeur
                df_mauvais.loc[indice_ligne_csv, "modifie"] = 1  # flag pour re-géocodage
                compteur_modifications += 1

                # Mise à jour dans QGIS
                for entite in couche_geocoded.getFeatures():
                    if str(entite['result_name']).strip().lower() == ancienne_valeur.strip().lower():
                        couche_geocoded.changeAttributeValue(entite.id(), index_result_name, nouvelle_valeur)

                # Mise à jour dans le tableau Qt
                modele.setData(modele.index(indice_ligne_csv, 1), nouvelle_valeur)

            # Valider les modifications QGIS
            if edition_locale:
                couche_geocoded.commitChanges()

            couche_geocoded.triggerRepaint()

            # Sauvegarde du CSV
            df_mauvais.to_csv(mauvais_csv, index=False, encoding='utf-8-sig')

            # Message utilisateur
            QMessageBox.information(
                self,
                "Modifications enregistrées",
                f"{compteur_modifications} ligne(s) modifiée(s). Elles seront re-géocodées."
            )

        except Exception as e:
            import traceback
            print(traceback.format_exc())
            QMessageBox.critical(self, "Erreur", str(e))


