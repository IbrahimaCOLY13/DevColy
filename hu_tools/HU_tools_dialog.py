    def load_themes(self):
        """Charge les thèmes existants dans le QTreeView."""
        # Sauvegarder les largeurs de colonnes actuelles
        col0_width = self.Theme_tree.columnWidth(0)
        col1_width = self.Theme_tree.columnWidth(1)

        # Vider le modèle
        self.theme_model.clear()
        self.theme_model.setHorizontalHeaderLabels(['Thème/Couche', 'Style'])
        
        # Obtenir les thèmes de carte du projet
        map_themes = QgsProject.instance().mapThemeCollection().mapThemes()
        
        # Pour chaque thème, ajouter un nœud parent
        for theme_name in map_themes:
            # Créer l'élément parent pour le thème
            theme_item = QStandardItem(theme_name)
            theme_item.setData("theme", Qt.UserRole)  # Type de l'élément
            
            # Obtenir la configuration du thème
            theme_collection = QgsProject.instance().mapThemeCollection()
            theme_data = theme_collection.mapThemeState(theme_name)
            
            # Parcourir les couches du thème
            for layer_record in theme_data.layerRecords():
                layer = layer_record.layer()
                # Ne traiter que les couches visibles
                if layer and layer_record.isVisible:
                    # Créer l'élément pour la couche
                    layer_item = QStandardItem(layer.name())
                    layer_item.setData("layer", Qt.UserRole)  # Type de l'élément
                    layer_item.setData(layer.id(), Qt.UserRole + 1)  # ID de la couche
                    
                    # Ajouter le style actuel
                    style_name = layer_record.currentStyle
                    style_item = QStandardItem(style_name)
                    style_item.setData(layer_record.usingCurrentStyle, Qt.UserRole + 2)
                    
                    # Ajouter une icône pour montrer que la couche est visible
                    layer_item.setIcon(QtWidgets.QApplication.style().standardIcon(QtWidgets.QStyle.SP_DialogApplyButton))
                    
                    # Ajouter la ligne à l'arbre
                    theme_item.appendRow([layer_item, style_item])
            
            # Ajouter une ligne spéciale pour ajouter de nouvelles couches
            add_layer_item = QStandardItem("+ Ajouter une couche")
            add_layer_item.setData("add_layer", Qt.UserRole)
            add_layer_item.setForeground(QColor(0, 128, 0))  # Vert pour distinguer
            theme_item.appendRow([add_layer_item, QStandardItem("")])
            
            # Ajouter le thème au modèle
            self.theme_model.appendRow(theme_item)
            
        # Restaurer les largeurs de colonnes
        if col0_width > 0 and col1_width > 0:
            self.Theme_tree.setColumnWidth(0, col0_width)
            self.Theme_tree.setColumnWidth(1, col1_width)

        # Étendre tous les thèmes
        #self.Theme_tree.expandAll()

    def add_new_theme(self):
        """Ajoute un nouveau thème."""
        # Demander le nom du thème
        theme_name, ok = QtWidgets.QInputDialog.getText(
            self, "Nouveau thème", "Nom du thème:"
        )
        
        if ok and theme_name:
            # Vérifier si le thème existe déjà
            theme_collection = QgsProject.instance().mapThemeCollection()
            if theme_name in theme_collection.mapThemes():
                QtWidgets.QMessageBox.warning(self, "Attention", f"Le thème '{theme_name}' existe déjà.")
                return
            
            # Créer une configuration vide pour le thème
            theme_state = QgsMapThemeCollection.MapThemeRecord()
            
            # Ajouter le thème à la collection
            theme_collection.insert(theme_name, theme_state)
            
            # Rafraîchir l'arborescence
            self.load_themes()
            
            # Sélectionner le nouveau thème
            self.select_theme_by_name(theme_name)
    
    def update_theme(self):
        """Met à jour le thème sélectionné ou ajoute une couche à un thème."""
        # Obtenir l'élément sélectionné
        selected_indexes = self.Theme_tree.selectionModel().selectedIndexes()
        if not selected_indexes:
            QtWidgets.QMessageBox.warning(self, "Attention", "Veuillez sélectionner un thème ou une couche.")
            return
        
        # Obtenir le modèle et l'index sélectionné
        index = selected_indexes[0]
        item = self.theme_model.itemFromIndex(index)
        
        # Déterminer si c'est un thème ou une couche
        item_type = item.data(Qt.UserRole)
        
        if item_type == "theme":
            self.add_layer_to_theme(item.text())
        elif item_type == "layer":
            # Trouver le thème parent
            parent_item = item.parent()
            if parent_item:
                theme_name = parent_item.text()
                layer_id = item.data(Qt.UserRole + 1)
                self.update_layer_in_theme(theme_name, layer_id)
        else:
            QtWidgets.QMessageBox.warning(self, "Attention", "Sélection non valide.")
    
    def add_layer_to_theme(self, theme_name):
        """Ajoute une couche au thème spécifié."""
        # Créer une boîte de dialogue pour sélectionner une couche
        layer_dialog = QtWidgets.QDialog(self)
        layer_dialog.setWindowTitle(f"Ajouter une couche au thème '{theme_name}'")
        layout = QtWidgets.QVBoxLayout()
        
        # Liste déroulante des couches
        layer_combo = QtWidgets.QComboBox()
        layout.addWidget(QtWidgets.QLabel("Sélectionner une couche:"))
        layout.addWidget(layer_combo)
        
        # Visibilité
        visibility_check = QtWidgets.QCheckBox("Visible")
        visibility_check.setChecked(True)
        layout.addWidget(visibility_check)
        
        # Liste déroulante des styles (sera remplie après sélection de la couche)
        style_combo = QtWidgets.QComboBox()
        layout.addWidget(QtWidgets.QLabel("Sélectionner un style:"))
        layout.addWidget(style_combo)
        
        # Boutons
        buttons = QtWidgets.QDialogButtonBox(
            QtWidgets.QDialogButtonBox.Ok | QtWidgets.QDialogButtonBox.Cancel
        )
        layout.addWidget(buttons)
        
        layer_dialog.setLayout(layout)
        
        # Remplir la liste des couches
        layers = QgsProject.instance().mapLayers().values()
        for layer in layers:
            layer_combo.addItem(layer.name(), layer.id())
        
        # Fonction pour mettre à jour les styles lorsqu'une couche est sélectionnée
        def update_styles():
            style_combo.clear()
            layer_id = layer_combo.currentData()
            if layer_id:
                layer = QgsProject.instance().mapLayer(layer_id)
                if layer:
                    styles = layer.styleManager().styles()
                    for style in styles:
                        style_combo.addItem(style)
        
        # Connecter le signal de changement de couche
        layer_combo.currentIndexChanged.connect(update_styles)
        
        # Initialiser les styles pour la couche sélectionnée
        update_styles()
        
        # Connecter les boutons
        buttons.accepted.connect(layer_dialog.accept)
        buttons.rejected.connect(layer_dialog.reject)
        
        # Exécuter la boîte de dialogue
        result = layer_dialog.exec_()
        
        if result == QtWidgets.QDialog.Accepted:
            # Récupérer les valeurs sélectionnées
            layer_id = layer_combo.currentData()
            style_name = style_combo.currentText()
            is_visible = visibility_check.isChecked()
            
            # Obtenir la collection de thèmes
            theme_collection = QgsProject.instance().mapThemeCollection()
            
            # Récupérer l'état actuel du thème
            theme_state = theme_collection.mapThemeState(theme_name)
            
            # Ajouter ou mettre à jour la couche dans le thème
            layer = QgsProject.instance().mapLayer(layer_id)
            if layer:
                # Vérifier si la couche existe déjà dans le thème
                exists = False
                for i, record in enumerate(theme_state.layerRecords()):
                    if record.layer().id() == layer_id:
                        # Mettre à jour l'enregistrement existant
                        record.currentStyle = style_name
                        record.isVisible = is_visible
                        theme_state.layerRecords()[i] = record
                        exists = True
                        break
                
                if not exists:
                    # Créer un nouvel enregistrement
                    layer_record = QgsMapThemeCollection.MapThemeLayerRecord(layer)
                    layer_record.currentStyle = style_name  
                    layer_record.isVisible = is_visible     
                    theme_state.addLayerRecord(layer_record)
                
                # Mettre à jour le thème
                theme_collection.update(theme_name, theme_state)
                
                # Rafraîchir l'arborescence
                self.load_themes()
                
                # Sélectionner le thème mis à jour
                self.select_theme_by_name(theme_name)
    
    def update_layer_in_theme(self, theme_name, layer_id):
        """Met à jour les propriétés d'une couche dans un thème."""
        # Obtenir la couche
        layer = QgsProject.instance().mapLayer(layer_id)
        if not layer:
            QtWidgets.QMessageBox.warning(self, "Attention", "Couche non trouvée.")
            return
        
        # Créer une boîte de dialogue pour modifier les propriétés de la couche
        layer_dialog = QtWidgets.QDialog(self)
        layer_dialog.setWindowTitle(f"Modifier '{layer.name()}' dans le thème '{theme_name}'")
        layout = QtWidgets.QVBoxLayout()
        
        # Visibilité
        visibility_check = QtWidgets.QCheckBox("Visible")
        layout.addWidget(visibility_check)
        
        # Liste déroulante des styles
        style_combo = QtWidgets.QComboBox()
        layout.addWidget(QtWidgets.QLabel("Style:"))
        layout.addWidget(style_combo)
        
        # Boutons
        buttons = QtWidgets.QDialogButtonBox(
            QtWidgets.QDialogButtonBox.Ok | QtWidgets.QDialogButtonBox.Cancel
        )
        layout.addWidget(buttons)
        
        # Bouton Supprimer
        delete_button = QtWidgets.QPushButton("Supprimer du thème")
        layout.addWidget(delete_button)
        
        layer_dialog.setLayout(layout)
        
        # Remplir la liste des styles
        styles = layer.styleManager().styles()
        for style in styles:
            style_combo.addItem(style)
        
        # Obtenir l'état actuel du thème
        theme_collection = QgsProject.instance().mapThemeCollection()
        theme_state = theme_collection.mapThemeState(theme_name)
        
        # Trouver l'enregistrement de la couche dans le thème
        current_style = ""
        is_visible = True
        for record in theme_state.layerRecords():
            if record.layer().id() == layer_id:
                current_style = record.currentStyle  # Retiré les parenthèses
                is_visible = record.isVisible  # Retiré les parenthèses
                break
        
        # Définir les valeurs initiales
        style_index = style_combo.findText(current_style)
        if style_index >= 0:
            style_combo.setCurrentIndex(style_index)
        visibility_check.setChecked(is_visible)
        
        # Connecter les boutons
        buttons.accepted.connect(layer_dialog.accept)
        buttons.rejected.connect(layer_dialog.reject)
        
        # Fonction pour supprimer la couche du thème
        def remove_layer():
            result = QtWidgets.QMessageBox.question(
                layer_dialog,
                "Confirmation",
                f"Voulez-vous vraiment supprimer '{layer.name()}' du thème '{theme_name}'?",
                QtWidgets.QMessageBox.Yes | QtWidgets.QMessageBox.No
            )
            
            if result == QtWidgets.QMessageBox.Yes:
                # Supprimer la couche du thème
                new_records = []
                for record in theme_state.layerRecords():
                    if record.layer().id() != layer_id:
                        new_records.append(record)
                
                # Créer un nouvel état avec les enregistrements filtrés
                new_state = QgsMapThemeCollection.MapThemeRecord()
                for record in new_records:
                    new_state.addLayerRecord(record)
                
                # Mettre à jour le thème
                theme_collection.update(theme_name, new_state)
                
                # Fermer la boîte de dialogue
                layer_dialog.reject()
                
                # Rafraîchir l'arborescence
                self.load_themes()
                
                # Sélectionner le thème mis à jour
                self.select_theme_by_name(theme_name)
        
        # Connecter le bouton de suppression
        delete_button.clicked.connect(remove_layer)
        
        # Exécuter la boîte de dialogue
        result = layer_dialog.exec_()
        
        if result == QtWidgets.QDialog.Accepted:
            # Récupérer les valeurs sélectionnées
            style_name = style_combo.currentText()
            is_visible = visibility_check.isChecked()
            
            # Mettre à jour l'enregistrement de la couche
            new_records = []
            for record in theme_state.layerRecords():
                if record.layer().id() == layer_id:
                    record.currentStyle = style_name
                    record.isVisible = is_visible
                new_records.append(record)
            
            # Créer un nouvel état avec les enregistrements mis à jour
            new_state = QgsMapThemeCollection.MapThemeRecord()
            for record in new_records:
                new_state.addLayerRecord(record)
            
            # Mettre à jour le thème
            theme_collection.update(theme_name, new_state)
            
            # Rafraîchir l'arborescence
            self.load_themes()
            
            # Sélectionner le thème mis à jour
            self.select_theme_by_name(theme_name)
    
    def select_theme_by_name(self, theme_name):
        """Sélectionne un thème dans l'arborescence par son nom."""
        for row in range(self.theme_model.rowCount()):
            item = self.theme_model.item(row, 0)
            if item.text() == theme_name:
                index = self.theme_model.indexFromItem(item)
                self.Theme_tree.selectionModel().select(
                    index, QItemSelectionModel.ClearAndSelect  # Utiliser QItemSelectionModel directement, pas via QtWidgets
                )
                self.Theme_tree.expand(index)
                break
    
