    def convertir_champ(self, type_field, type_name, need_precision=False):
        """Fonction générique pour convertir un ou plusieurs champs."""
        QgsMessageLog.logMessage(">>> Début de la fonction de conversion <<<", "ConversionChamps")
        # Vérifier qu'au moins une ligne est sélectionnée
        selected_rows = self.Champview.selectionModel().selectedRows()
        if not selected_rows:
            QMessageBox.warning(self, "Attention", "Veuillez sélectionner au moins un champ à convertir.")
            return
            
        # Convertir les indices du proxy model en indices du modèle source
        source_rows = [self.proxy_model.mapToSource(row) for row in selected_rows]
        
        # Obtenir la longueur spécifiée
        longueur = self.Longueur.value()
        if longueur <= 0:
            QMessageBox.warning(self, "Attention", "Veuillez spécifier une longueur valide (supérieure à 0).")
            return
            
        # Vérifier la précision si nécessaire
        precision = self.Precision.value() if need_precision else 0
        if need_precision and precision < 0:
            QMessageBox.warning(self, "Attention", "Veuillez spécifier une précision valide (supérieure ou égale à 0).")
            return
            
        # Obtenir la couche active
        layer_id = self.Listecouche.currentData()
        layer = QgsProject.instance().mapLayer(layer_id)
        
        if not layer:
            return
            
        # Commencer l'édition de la couche                                
        if not layer.isEditable():
            layer.startEditing()
        
        success_count = 0
        error_count = 0
        error_fields = []
        
        # Traiter chaque champ sélectionné
        for row in source_rows:
            # Obtenir le nom du champ sélectionné
            field_name = self.champs_model.item(row.row(), 0).text()
            field_index = layer.fields().indexOf(field_name)
            
            # Créer le nouveau champ temporaire
            new_field_name = f"{field_name}_temp"
            new_field = QgsField(new_field_name, type_field)
            new_field.setLength(longueur)
            if need_precision:
                new_field.setPrecision(precision)            
            # Ajouter le nouveau champ
            if layer.addAttribute(new_field):
                new_field_index = layer.fields().indexOf(new_field_name)
                
                # Copier les valeurs avec conversion
                for feature in layer.getFeatures():
                    old_value = feature[field_index]
                    try:
                        if type_field == QVariant.Int:
                            # Conversion en entier
                            new_value = int(float(old_value)) if old_value else None

                        elif type_field == QVariant.Double:
                            # Conversion en nombre décimal
                            new_value = float(old_value) if old_value else None
                        
                    except (ValueError, TypeError):
                        # Capture toute autre erreur inattendue (ex. type non prévu)
                        new_value = None
                        import traceback
                        QgsMessageLog.logMessage(
                            f"Erreur de conversion pour le champ '{field_name}', valeur '{old_value}': {traceback.format_exc()}",
                            "ConversionChamps", Qgis.Warning
                        )

                    # Mise à jour de la valeur convertie
                    layer.changeAttributeValue(feature.id(), new_field_index, new_value)
                # Supprimer l'ancien champ
                layer.deleteAttribute(field_index)
                        
                # Renommer le nouveau champ
                idx = layer.fields().indexOf(new_field_name)
                if layer.renameAttribute(idx, field_name):
                    success_count += 1
                else:
                    error_count += 1
                    error_fields.append(field_name)
                
            else:
                error_count += 1
                error_fields.append(field_name)
            
        # Sauvegarder les modifications
        if layer.commitChanges():
            
            msg = f"{success_count} champ(s) converti(s) en {type_name}"

            if need_precision:
                msg += f" avec une longueur de {longueur} et une précision de {precision}."
            else:
                msg += f" avec une longueur de {longueur}."
            if error_count > 0:
                msg += f"\n\nErreur sur {error_count} champ(s): {', '.join(error_fields)}"

            if success_count > 0:
                QMessageBox.information(self, "Résultat", msg)
            else:
                QMessageBox.warning(self, "Erreur", msg)
                
            # Rafraîchir l'affichage des champs
            self.afficher_champs()
        else:
            QMessageBox.warning(self, "Erreur", "Impossible de sauvegarder les modifications.")
            layer.rollBack()                                                                                                                                                         

