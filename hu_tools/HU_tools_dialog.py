    # Parse période "YYYY-YYYY"
    @staticmethod
    def _parse_periode(periode: str):
        m = re.match(r"^\s*(\d{4})\s*-\s*(\d{4})\s*$", periode)
        if not m:
            return None
        a, b = int(m.group(1)), int(m.group(2))
        if a > b:
            a, b = b, a
        return a, b

