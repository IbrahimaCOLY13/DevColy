    #3) fonction de zoom sur l'erreur topologique
    def zoom_erreur_topo(self):
        """Zoom précis sur l'erreur topologique sélectionnée sans jamais dézoomer."""
        current_row = self.topo_table_erreurs.currentRow()
        if current_row < 0:
            return

        geom = self.topo_table_erreurs.item(current_row, 0).data(Qt.UserRole)
        if not geom:
            return

        layer_name = self.topo_table_erreurs.item(current_row, 2).text()
        layers = QgsProject.instance().mapLayersByName(layer_name)
        layer = layers[0] if layers else None

        # Reprojection éventuelle
        try:
            project_crs = QgsProject.instance().crs()
            geom_to_zoom = QgsGeometry(geom)

            if layer and layer.crs() and layer.crs() != project_crs:
                transform = QgsCoordinateTransform(layer.crs(), project_crs, QgsProject.instance())
                geom_to_zoom.transform(transform)
        except:
            geom_to_zoom = QgsGeometry(geom)

        #  étendue + point central 
        bbox = geom_to_zoom.boundingBox()
        # On calcule le centre : toujours fiable
        center = bbox.center()
        canvas = iface.mapCanvas()

        # Centrer toujours en premier 
        canvas.setCenter(QgsPointXY(center.x(), center.y()))
        # Empêcher le dézoom , scale minimum 
        MIN_SCALE = 1500  # on peut descendre à 500 si on veut plus proche
        
        try:
            current_scale = canvas.scale()
            if current_scale > MIN_SCALE:
                canvas.zoomScale(MIN_SCALE)
        except:
            canvas.zoomScale(MIN_SCALE)

        # pour Marquer la géométrie en rouge 
        geom_type = geom_to_zoom.type()
        if geom_type == QgsWkbTypes.PointGeometry:
            rb_type = QgsWkbTypes.PointGeometry
        elif geom_type == QgsWkbTypes.LineGeometry:
            rb_type = QgsWkbTypes.LineGeometry
        else:
            rb_type = QgsWkbTypes.PolygonGeometry

        # Reset du précédent rubber band
        if hasattr(self, "rubber_band") and self.rubber_band:
            self.rubber_band.reset(rb_type)

        self.rubber_band = QgsRubberBand(canvas, rb_type)
        self.rubber_band.setColor(QColor(255, 255, 0, 200))
        self.rubber_band.setWidth(4) # ppour bien le voir
        self.rubber_band.setToGeometry(geom_to_zoom, None)
        canvas.refresh()
    
        
    # Exporter les erreurs topologiques vers Excel
    def export_erreur_topo(self):
        """Exporte les erreurs topologiques en format Excel de manière robuste."""
        file_path, _ = QFileDialog.getSaveFileName(
            self, "Exporter les erreurs", "", "Fichiers Excel (*.xlsx)"
        )
        if not file_path:
            return
        if not file_path.lower().endswith('.xlsx'):
            file_path += '.xlsx'

        try:
            data = []
            rowcount = self.topo_table_erreurs.rowCount()
            for row in range(rowcount):
                item_type = self.topo_table_erreurs.item(row, 0)
                item_desc = self.topo_table_erreurs.item(row, 1)
                item_layer = self.topo_table_erreurs.item(row, 2)
                item_id = self.topo_table_erreurs.item(row, 3)

                if not item_type or not item_layer or not item_id:
                    continue

                layer_name = item_layer.text() or ""
                try:
                    feature_id = int(item_id.text())
                except Exception:
                    feature_id = None

                error_data = {
                    'Type': item_type.text() if item_type else '',
                    'Description': item_desc.text() if item_desc else '',
                    'Couche': layer_name,
                    'ID': str(item_id.text()) if item_id else ''
                }

                # récupérer attributs de l'entité sans crash
                try:
                    layers = QgsProject.instance().mapLayersByName(layer_name)
                    layer = layers[0] if layers else None
                    if layer and feature_id is not None:
                        feature = next(layer.getFeatures(QgsFeatureRequest(feature_id)), None)
                        if feature:
                            for fld in layer.fields():
                                fname = fld.name()
                                try:
                                    val = feature[fname]
                                    error_data[f"Attribut_{fname}"] = '' if val is None else str(val)
                                except Exception:
                                    error_data[f"Attribut_{fname}"] = ''
                except Exception:
                    pass
                data.append(error_data)

            if not data:
                QMessageBox.information(self, "Export", "Aucune erreur à exporter.")
                return

            import pandas as pd
            df = pd.DataFrame(data)

            # Version "safe" : éviter toute configuration de styles Openpyxl
            df.to_excel(file_path, index=False)

            QtWidgets.QMessageBox.information(
                self,
                "Export réussi",
                f"Les erreurs ont été exportées vers {file_path}"
            )
        except Exception as e:
            QtWidgets.QMessageBox.warning(
                self,
                "Erreur d'export",
                f"Une erreur est survenue lors de l'export: {str(e)}"
            )
