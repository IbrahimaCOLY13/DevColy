    def sauvegarder_etat_arbre(self):
        """Sauvegarde l’état d’expansion de tous les dossiers, à tous les niveaux."""
        etats = []
        def parcourir(item):
            if item is None:
                return
            index = self.style_model.indexFromItem(item)
            # Enregistrer l'état uniquement si c'est un dossier
            type_item = item.data(Qt.UserRole)
            if type_item == "directory":
                etats.append((self.Style_tree.isExpanded(index),
                              self.chemin_item(item)))
            # Parcourir les enfants
            for i in range(item.rowCount()):
                enfant = item.child(i)
                parcourir(enfant)
        racine = self.style_model.item(0, 0)
        if racine:
            parcourir(racine)

        return etats

    def restaurer_etat_arbre(self, etats):
        """Restaure l’état d’expansion de tous les dossiers."""
        if not etats:
            return
        for etat_ouvert, chemin in etats:
            item = self.retrouver_item_par_chemin(chemin)
            if item:
                index = self.style_model.indexFromItem(item)
                if etat_ouvert:
                        self.Style_tree.expand(index)
                        
    def chemin_item(self, item):
        """Retourne le chemin complet (liste des noms) de la racine à l’item."""
        chemin = []
        courant = item
        while courant:
            chemin.insert(0, courant.text())
            courant = courant.parent()
        return chemin

    def retrouver_item_par_chemin(self, chemin):
        """Retrouve un item dans l'arborescence à partir de son chemin."""
        if not chemin:
            return None
        item = self.style_model.item(0, 0)  # la racine "Styles"
        if not item or item.text() != chemin[0]:
            return None
        for nom in chemin[1:]:
            trouve = False
            for i in range(item.rowCount()):
                enfant = item.child(i)
                if enfant and enfant.text() == nom:
                    item = enfant
                    trouve = True
                    break
            if not trouve:
                return None

        return item

        
    def load_style_tree(self):
        """Charge l'arborescence des fichiers QML depuis le dossier style, 
        filtrés selon le type de géométrie de la couche sélectionnée."""
        # Sauvegarder l’état avant modification
        etat_precedent = self.sauvegarder_etat_arbre()
    
        # Vider le modèle pour le reconstruire 
        self.style_model.clear()
        self.style_model.setHorizontalHeaderLabels(['Styles disponibles'])
        
        # Chemin vers le dossier style
        style_dir = os.path.join(os.path.dirname(__file__), 'style')
        # Créer le dossier s'il n'existe pas
        if not os.path.exists(style_dir):
            os.makedirs(style_dir)
        
        # Obtenir le type de géométrie de la couche sélectionnée
        geom_type = None
        layer_id = self.Style_couche.currentData()
        if layer_id:
            layer = QgsProject.instance().mapLayer(layer_id)
            if layer and isinstance(layer, QgsVectorLayer):
                geom_type = layer.geometryType()
        
        # Créer l'élément racine
        root_item = QStandardItem("Styles")
        self.style_model.appendRow(root_item)

        # Fonction récursive pour ajouter les dossiers et fichiers
        def add_directory_items(parent_item, directory):
            # Lister les QML directement dans ce dossier (pas dans les sous-dossiers)
            qmls_in_folder = [
                entry.path for entry in os.scandir(directory)
                if entry.is_file() and entry.name.lower().endswith('.qml')
            ]

            # Identifier les types présents dans ce dossier
            type_mapping = {"point": "Points", "line": "Lignes", "polygon": "Polygones"}
            types_present = set()
            for qml in qmls_in_folder:
                geom = extract_qml_geometrie_type(qml)
                if geom:
                    types_present.add(geom)

            # pour Créer d'abord les dossiers virtuels (si QML présents ici)
            created_type_folders = {}
            for geom in types_present:
                folder_name = type_mapping.get(geom)
                if folder_name:
                    type_item = QStandardItem(folder_name)
                    type_item.setData("directory", Qt.UserRole)
                    type_item.setData(directory, Qt.UserRole + 1)
                    parent_item.appendRow(type_item)
                    created_type_folders[geom] = type_item

            #ajouter les fichiers QML dans les bons dossiers
            for qml_path in qmls_in_folder:
                if self.is_style_compatible_with_layer(qml_path, geom_type):
                    file_item = QStandardItem(os.path.basename(qml_path))
                    file_item.setData("file", Qt.UserRole)
                    file_item.setData(qml_path, Qt.UserRole + 1)

                    geom = extract_qml_geometrie_type(qml_path)
                    if geom in created_type_folders:
                        created_type_folders[geom].appendRow(file_item)
                    else:
                        parent_item.appendRow(file_item)

            # Ajouter ensuite les sous-dossiers réels
            for entry in os.scandir(directory):
                if entry.is_dir():
                    folder_item = QStandardItem(entry.name)
                    folder_item.setData("directory", Qt.UserRole)
                    folder_item.setData(entry.path, Qt.UserRole + 1)
                    parent_item.appendRow(folder_item)

                    # Recurse
                    add_directory_items(folder_item, entry.path)

        # Ajouter tous les éléments
        add_directory_items(root_item, style_dir)
        
        # Développer le dossier racine
        self.Style_tree.expand(self.style_model.indexFromItem(root_item))
        # pour empêcher l’arborescence de se déplier
        # Reassigner le modèle
        self.Style_tree.setModel(self.style_model)
        # Restaurer l’état
        self.restaurer_etat_arbre(etat_precedent)


    def is_style_compatible_with_layer(self, qml_path, layer_geom_type):
        """Détermine si un fichier de style QML est compatible avec le type de géométrie de la couche."""
        # Si aucune couche n'est sélectionnée, montrer tous les styles
        if layer_geom_type is None:
            return True
        
        try:
            # Essayer de déterminer la géométrie ciblée par le QML en lisant son contenu
            with open(qml_path, 'r', encoding='utf-8') as f:
                content = f.read()
                
                # Déterminer le type de géométrie du style par les motifs dans le fichier
                qml_is_point = any(marker in content.lower() for marker in ['marker', 'symbol', 'point', 'caractéristiquessimplepoint'])
                qml_is_line = any(line in content.lower() for line in ['line', 'ligne', 'caractéristiquessimpleligne'])
                qml_is_polygon = any(polygon in content.lower() for polygon in ['fill', 'polygon', 'polygone', 'caractéristiquessimplepolygon'])
                
                # Si le QML ne contient aucun indice de géométrie, supposer qu'il est générique
                if not (qml_is_point or qml_is_line or qml_is_polygon):
                    return True
                
                # Vérifier la compatibilité avec la géométrie de la couche
                if layer_geom_type == QgsWkbTypes.PointGeometry and qml_is_point:
                    return True
                elif layer_geom_type == QgsWkbTypes.LineGeometry and qml_is_line:
                    return True
                elif layer_geom_type == QgsWkbTypes.PolygonGeometry and qml_is_polygon:
                    return True
                
                # Style non compatible
                return False
        
        except Exception as e:
            print(f"Erreur lors de la vérification du style {qml_path}: {str(e)}")
            # En cas d'erreur, montrer le style pour éviter de masquer des styles potentiellement valides
            return True

